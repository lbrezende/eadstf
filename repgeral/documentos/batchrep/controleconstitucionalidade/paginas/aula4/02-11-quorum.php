				<h4>2.11 Quórum</h4>
			</div>
			<div class="col s12 m8 offset-m2 l6 offset-l3">
				
				<p>Devido ao impacto de uma decisão que declara a inconstitucionalidade de lei ou ato normativo, a Lei 9.868/1999 teve o cuidado de exigir que a ação direta de inconstitucionalidade somente possa ser julgada se presentes na sessão pelo menos oito Ministros. Além disso, é necessário alcançar pelo menos seis votos para que o Tribunal declare a constitucionalidade ou inconstitucionalidade de um dispositivo. Se isso não ocorrer, o julgamento é suspenso para se aguardar o voto dos Ministros ausentes.</p>
				
				<p>Na ADI 3.510, foi atingido o quórum mínimo para a declaração da constitucionalidade do artigo 5º da Lei de Biossegurança, com o voto de seis dos onze Ministros do Supremo Tribunal Federal.</p>
				
