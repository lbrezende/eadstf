				<p>Entretanto, o Tribunal entendeu ausentes os requisitos de generalidade e abstração no Decreto 6.161/2007, ato normativo objeto da ADI 4.040:</p>
				
				<blockquote class="legislacao">
					
					<p>AÇÃO DIRETA DE INCONSTITUCIONALIDADE. DECRETO N. 6.161/2007, ALTERADO PELO DECRETO N. 6.267/2007, QUE “DISPÕE SOBRE A INCLUSÃO E EXCLUSÃO, NO PROGRAMA NACIONAL DE DESESTATIZAÇÃO — PND, DE EMPREENDIMENTOS DE TRANSMISSÃO DE ENERGIA ELÉTRICA INTEGRANTES DA REDE BÁSICA DO SISTEMA ELÉTRICO INTERLIGADO NACIONAL — SIN, DETERMINA À AGÊNCIA NACIONAL DE ENERGIA ELÉTRICA — ANEEL PROMOÇÃO E O ACOMPANHAMENTO DOS PROCESSOS DE LICITAÇÃO DAS RESPECTIVAS CONCESSÕES”. (...) Impossibilidade de ajuizamento de ação direta de inconstitucionalidade contra ato normativo de efeito concreto. O Decreto n. 6.161/2007, alterado pelo Decreto n. 6.267/2007 não se dota das características de abstração e generalidade para ser processado e julgado pela via eleita. 3. Ação direta de inconstitucionalidade não conhecida. </p>
					
					<p>(ADI 4.040/DF, Relatora Ministra Cármen Lúcia, Tribunal Pleno, julgamento em 19-6-2013, <em>DJe</em> 1º-7-2013)</p>
					
