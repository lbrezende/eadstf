				<p>Vejamos dois exemplos da jurisprudência do Supremo Tribunal Federal. Ao apreciar a ADI 4.965, o STF entendeu cabível a ação direta de inconstitucionalidade para questionar a Resolução 23.389/2013 do Tribunal Superior Eleitoral:</p>
				
				<blockquote class="legislacao">
					
					<p>Segundo a jurisprudência desta Suprema Corte, viável o controle abstrato da constitucionalidade de ato do Tribunal Superior Eleitoral de conteúdo jurídico-normativo essencialmente primário. A Resolução nº 23.389/2013 do TSE, ao inaugurar conteúdo normativo primário com abstração, generalidade e autonomia não veiculado na Lei Complementar nº 78/1993 nem passível de ser dela deduzido, em afronta ao texto constitucional a que remete — o art. 45, <em>caput</em> e § 1º, da Constituição Federal —, expõe-se ao controle de constitucionalidade concentrado. Precedentes (...).</p>
					
					<p class="citacao">(<strong>ADI 4.965/PB</strong>, Relatora Ministra Rosa Weber, Tribunal Pleno, julgamento em 1º-7-2014, <em>DJe</em> de 30-10-2014)</p>
					
				</blockquote>
				
