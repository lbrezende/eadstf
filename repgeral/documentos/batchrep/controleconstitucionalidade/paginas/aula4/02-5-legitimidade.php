				<h4>2.5 Legitimidade ativa</h4>
			</div>
			<div class="col s12 l8 offset-l2">
				
				<p>Como regra geral, nos processos subjetivos, é do próprio interessado, o titular dos direitos em jogo, a legitimidade para iniciar o processo. Mas, se o processo iniciado com o ajuizamento de uma ação direta de inconstitucionalidade é um <strong>processo objetivo</strong>, que não tem a finalidade de decidir a respeito da titularidade de <strong>direitos subjetivos</strong> (do sujeito) daquele que faz uso do instrumento, quem é que pode propor a ação direta de inconstitucionalidade? Quem é que pode provocar o Supremo Tribunal Federal para decidir sobre a validade de uma lei em face da Constituição?</p>
				
				<p>A Constituição restringe a propositura da ação direta de inconstitucionalidade — <strong>legitimidade processual ativa</strong> — a determinados agentes políticos e sociais cujas atividades estão ligadas à preservação da constitucionalidade das normas. Assim, podem propor a ação direta de inconstitucionalidade, segundo o artigo 103 da Constituição:</p>
				
				<blockquote>
					<p><strong>I</strong> — o Presidente da República;</p>
					<p><strong>II</strong> — a Mesa do Senado Federal;</p>
					<p><strong>III</strong> — a Mesa da Câmara dos Deputados;</p>
					<p><strong>IV</strong> — a Mesa de Assembleia Legislativa ou da Câmara Legislativa do Distrito Federal;</p>
					<p><strong>V</strong> — o Governador de Estado ou do Distrito Federal;</p>
					<p><strong>VI</strong> — o Procurador-Geral da República;</p>
					<p><strong>VII</strong> — o Conselho Federal da Ordem dos Advogados do Brasil;</p>
					<p><strong>VIII</strong> — partido político com representação no Congresso Nacional;</p>
					<p><strong>IX</strong> — confederação sindical ou entidade de classe de âmbito nacional.</p>
				</blockquote>
				
