				<h4>2.7 Como se dá o processamento da ação direta de inconstitucionalidade</h4>
			</div>
			<div class="col s12 l6 offset-l3">
				
				<p>Proposta a ação direta de inconstitucionalidade, ela é distribuída por sorteio a um dos Ministros do Supremo Tribunal Federal, que será o seu Relator — à exceção do Presidente, que não assume relatoria de processos.</p>
				
				<p>O Relator será o responsável por cuidar do andamento do processo até que ele esteja pronto para julgamento e, como o nome diz, prepara o relatório para dar conhecimento do processo aos demais Ministros. Ele também avalia se há pedido de medida cautelar e a urgência na sua concessão. Isso vai determinar os próximos passos e os prazos a serem concedidos para as manifestações das autoridades envolvidas.</p>
				
				
