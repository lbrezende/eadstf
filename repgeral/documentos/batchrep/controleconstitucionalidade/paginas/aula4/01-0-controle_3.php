				<p>Como você já deve ter percebido, o objetivo do processo no qual se exerce o controle difuso não é discutir a constitucionalidade de uma lei. No <strong>processo subjetivo</strong>, o questionamento sobre a constitucionalidade da lei surge eventualmente, quando o juiz ou o Tribunal se vê diante da possibilidade de a aplicação de uma lei, no caso sob seu exame, se mostrar incompatível com a Constituição. Desde uma demanda trabalhista entre empregado e empregador a uma ação de indenização entre consumidor e fornecedor de um serviço, passando pelas ações penais em que o Ministério Público acusa alguém da prática de crime, o que se discute nesses processos são os direitos dos sujeitos que procuram a Justiça ou são nela demandados. Esses direitos podem ou não estar ligados com a validade de uma norma, que pode ou não vir a ser considerada inconstitucional.</p>
				
				<p>Diferentemente, no <strong>processo objetivo</strong>, em que se realiza o controle concentrado de constitucionalidade, não existe uma disputa entre dois sujeitos (autor e réu). O escopo desse processo é formalizar o procedimento pelo qual o Supremo Tribunal Federal aprecia e julga a constitucionalidade de uma lei ou ato normativo. A questão relativa à constitucionalidade da lei não é, nesse processo, apenas uma referência para decidir quem tem razão em uma disputa judicial, mas é a grande questão a ser decidida. O resultado dessa decisão será a exclusão ou permanência da lei na <?php _el('glossario','ordem jurídica');?>.</p>
				
