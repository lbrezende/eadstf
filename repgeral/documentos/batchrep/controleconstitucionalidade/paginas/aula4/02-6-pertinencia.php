				<h4>2.6 Pertinência temática</h4>
			</div>
			<div class="col s12 l8 offset-l2">
				
				<p>O Supremo Tribunal Federal tem o entendimento de que nem todos os legitimados do artigo 103 da CF podem propor ação direta de inconstitucionalidade para impugnar qualquer lei ou ato normativo.</p>
				
				<p>Destes, o Presidente da República, a Mesa do Senado Federal, a Mesa da Câmara dos Deputados, o Procurador-Geral da República, o Conselho Federal da Ordem dos Advogados do Brasil e os partidos políticos com representação no Congresso Nacional são chamados de <strong>legitimados universais</strong>. Podem impugnar, pela via da ação direta de inconstitucionalidade, qualquer lei ou ato normativo, federal ou estadual.</p>
				
				<p>Já o Governador de Estado, a Mesa de Assembleia Legislativa, o Governador do Distrito Federal e a Câmara Legislativa do Distrito Federal somente podem ajuizar ação direta de inconstitucionalidade contra leis e atos normativos da sua própria unidade da Federação. Para se insurgirem contra leis federais ou de outra unidade federativa, precisam demonstrar que aquela lei ou ato normativo afeta o seu Estado.</p>
				
				<p>Por sua vez, as confederações sindicais e as entidades de classe precisam demonstrar que a lei ou ato normativo contra o qual se insurgem dizem respeito às suas finalidades institucionais. Assim, uma confederação de empregados na indústria, em princípio, não teria interesse para impugnar uma lei sobre direito eleitoral. Já uma associação nacional de proteção dos consumidores, em princípio, teria interesse em discutir a constitucionalidade de uma lei que flexibilizasse critérios de qualidade da produção de determinados produtos.</p>
				
				<p>Uma vez provocado à atuação o Supremo Tribunal Federal, com a propositura da ADI, não pode o autor desistir da ação. Entende-se que a tutela do direito objetivo é mais relevante do que eventual flutuação no interesse subjetivo da parte que possa motivá-la a desistir. É a regra do artigo 5º da Lei 9.868/1999.</p>
				
			
