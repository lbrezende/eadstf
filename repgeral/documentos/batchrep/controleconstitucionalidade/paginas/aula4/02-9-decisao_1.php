				<p>Essa técnica foi empregada no julgamento da ADI 4.815, em que se discutiu a constitucionalidade das biografias não autorizadas. Na ação, foi pedida a interpretação conforme a Constituição dos <strong>artigos 20 e 21 do Código Civil</strong>, que tratam, respectivamente, do direito à imagem e à vida privada. O Supremo Tribunal Federal julgou a ação procedente para conferir a esses preceitos interpretação conforme os direitos fundamentais à liberdade de pensamento e de sua expressão, de criação artística e produção científica. Com isso, declarou que não se exige o consentimento da pessoa biografada ou das pessoas retratadas como coadjuvantes (ou de seus familiares no caso de pessoas falecidas) para a publicação ou veiculação de obras de cunho biográfico.</p>
				
