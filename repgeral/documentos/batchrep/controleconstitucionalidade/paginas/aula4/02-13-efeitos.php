				<h4>2.13 Modulação dos efeitos</h4>
			</div>
			<div class="col s12 l8 offset-l2">
				
				<p>Se uma lei é inconstitucional, ela não pode, em regra, produzir efeitos. Não pode afetar a vida das pessoas.</p>
				
				<p>Na prática, porém, ocorre de leis terem a sua inconstitucionalidade declarada pelo Supremo Tribunal Federal após um tempo considerável desde que foram editadas. Nesse intervalo, elas podem ter sido aplicadas para reger inúmeras relações jurídicas, e a declaração de inconstitucionalidade pode acabar gerando insegurança. O que fazer com todas as situações que se concretizaram, os negócios que foram realizados e as expectativas que foram geradas enquanto a lei estava em vigor e produzindo seus efeitos? Para esses casos, a lei permite ao Supremo Tribunal Federal, tendo em vista razões de segurança jurídica ou de excepcional interesse social, restringir os efeitos da decisão ou decidir que ela só tenha efeitos no futuro. Entretanto, para isso, são necessários pelo menos oito votos. É o que diz o artigo 27 da Lei 9.868/1999:</p>
				
				<div class="box box-citacao">
					<blockquote>
						<p><strong>Art. 27.</strong></p>
						<p>Ao declarar a inconstitucionalidade de lei ou ato normativo, e tendo em vista razões de segurança jurídica ou de excepcional interesse social, poderá o Supremo Tribunal Federal, por maioria de dois terços de seus membros, restringir os efeitos daquela declaração ou decidir que ela só tenha eficácia a partir de seu trânsito em julgado ou de outro momento que venha a ser fixado.</p>
					</blockquote>
