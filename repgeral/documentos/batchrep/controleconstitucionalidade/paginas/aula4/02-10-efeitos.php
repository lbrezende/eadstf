				<h4>2.10 Efeitos da decisão declaratória de inconstitucionalidade</h4>
			</div>
			<div class="col s12 l8 offset-l2">
				
				<p>A decisão do Supremo Tribunal Federal que, no julgamento da ação direta de inconstitucionalidade, declara a constitucionalidade ou a inconstitucionalidade de uma lei ou ato normativo, inclusive a interpretação conforme a Constituição e a declaração parcial de inconstitucionalidade sem redução de texto, tem duas características especialmente importantes: eficácia contra todos e efeito vinculante.</p>
				
				<p><strong>Eficácia contra todos</strong> significa que os efeitos da decisão valem para todo mundo. Assim como a lei constitucional vale para todos, a lei declarada inconstitucional não vale para ninguém. Todas as relações jurídicas que seriam regidas por ela deixam de sê-lo.</p>
				
				<p><strong>Efeito vinculante</strong> quer dizer que, uma vez declarada a constitucionalidade ou inconstitucionalidade da lei, os demais juízes e Tribunais, bem como a administração pública, ficam obrigados a segui-la. Após o julgamento da <strong>ADI 3.510</strong>, a pesquisa com células-tronco embrionárias não pode mais ser desautorizada, seja pelos órgãos administrativos competentes, seja pelo Poder Judiciário, sob o argumento da inconstitucionalidade da lei que a prevê.</p>
				
