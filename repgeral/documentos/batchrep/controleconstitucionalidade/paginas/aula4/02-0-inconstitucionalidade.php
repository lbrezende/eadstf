				<h3>2. Ação direta de inconstitucionalidade (ADI)</h3>
			</div>
			<div class="col s12 l6">
				
				<p>Podemos dizer, com segurança, que a ação direta de inconstitucionalidade, no sistema político-jurídico brasileiro, é o instrumento da jurisdição constitucional por excelência, diretamente relacionado à função do Supremo Tribunal Federal de guarda da Constituição. </p>
				
				<p>Está prevista no art. 102, I, <em>a</em>, da Constituição:</p>
				
				<div class="box box-destaque">
					<blockquote>
						<p>Art. 102. Compete ao Supremo Tribunal Federal, precipuamente, a guarda da Constituição, cabendo-lhe:</p>
						<p>I — processar e julgar, originariamente:</p>
						<p><strong>a)</strong> a ação direta de inconstitucionalidade de lei ou ato normativo federal ou estadual (...);</p>
						<p>(...)</p>
						<p><strong>p)</strong> o pedido de medida cautelar das ações diretas de inconstitucionalidade;</p>
					</blockquote>
				</div>
				
				<p>Como lhe é próprio, a Constituição não regula todos os aspectos da ação direta de inconstitucionalidade, limitando-se a prever a existência do instrumento e definir apenas as características procedimentais imprescindíveis para a preservação da sua essência.</p>
				
			</div>
			<div class="col s12 l6">
				
				<p>Assim, na própria Constituição estão expressamente previstos a possibilidade de concessão de <strong>medida cautelar</strong> na ADI (art. 102, I, <em>p</em>), a extensão dos efeitos das decisões do Supremo Tribunal nessa ação (art. 102, § 2º) e os <strong>legitimados</strong> para propô-la (art. 103). As demais regras processuais e procedimentais que disciplinam a ação direta de inconstitucionalidade foram definidas na <a href="https://goo.gl/NMRWEU" class="ui-link" rel="external" target="_blank">Lei 9.868/1999</a>.</p>
				
				<p>Com base nas cláusulas constitucionais pertinentes e na Lei 9.868/1999, vamos analisar os principais aspectos da ação direta de inconstitucionalidade a partir do julgamento de um caso real: a polêmica <strong>ADI 3.510</strong>, sobre a constitucionalidade das pesquisas com células-tronco embrionárias humanas.</p>
				
			</div>
