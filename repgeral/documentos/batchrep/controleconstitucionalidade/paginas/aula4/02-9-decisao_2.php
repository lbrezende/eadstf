				<p>O Tribunal preserva a constitucionalidade do texto da lei, mas exclui a sua aplicação a determinadas situações. A ação direta de inconstitucionalidade é julgada total ou parcialmente procedente, a depender da extensão do pedido.</p>
				
				<p>Isso pode acontecer, por exemplo, no caso de uma lei que tenha alterado a forma de cálculo de determinado imposto, majorando-o, sem especificar quando ele passará a ser cobrado segundo o novo critério. O artigo 150, III, <em>b</em>, da Constituição da República prevê o chamado princípio da anterioridade tributária, segundo o qual, uma vez criado ou aumentado um tributo, ele só pode ser cobrado a partir do exercício financeiro seguinte (correspondendo ao ano civil). Assim, um tributo sujeito a essa regra, criado em 2015, só pode ser cobrado a partir de 2016.</p>
				
				<p>Havendo dúvida, por algum motivo, quanto à sujeição da alteração trazida pela nova lei ao princípio da anterioridade, pode ela ser questionada via ação direta de inconstitucionalidade. Mesmo considerando a alteração, em si, constitucional, o Supremo Tribunal Federal pode, se entender aplicável ao caso o princípio da anterioridade, declarar a nulidade parcial da lei para afastar a sua cobrança no mesmo exercício financeiro em que publicada. O texto da lei é inteiramente preservado, mas a sua aplicação a determinada hipótese (no caso, a determinado exercício financeiro) é declarada nula.</p>
				
