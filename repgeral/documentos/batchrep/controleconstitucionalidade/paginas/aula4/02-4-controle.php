				<h4>2.4 Parâmetro de controle</h4>
			</div>
			<div class="col s12 l6">
				
				<p>O autor da ação direta de inconstitucionalidade deve indicar, na petição inicial, o dispositivo da Constituição que entende ter sido contrariado pela lei questionada. No caso, o Procurador-Geral da República apontou que o artigo 5º da Lei de Biossegurança seria contrário aos conteúdos dos artigos 1º, III, e 5º, <em>caput</em>, da Constituição Federal, que consagram, respectivamente, “a dignidade da pessoa humana” e “a inviolabilidade do direito à vida”.</p>
				
				<p>Se o Supremo Tribunal Federal tivesse acatado a argumentação do Procurador-Geral da República nesse caso, estaríamos diante de uma declaração de <strong>inconstitucionalidade material</strong>: quando o conteúdo de uma lei é incompatível com o conteúdo da Constituição.</p>
				
				<p>Ao lado da material, há, também, a <strong>inconstitucionalidade formal</strong>: quando a inconstitucionalidade não está no conteúdo da lei, mas na forma como esse conteúdo foi veiculado. Como estudamos na Aula 2, a Constituição estabelece as regras do processo legislativo. Há, por exemplo, matérias que só podem ser objeto de lei complementar. A edição de lei ordinária sobre essas matérias é uma hipótese de inconstitucionalidade formal. Como o problema, aí, não é o conteúdo, mas a forma, os mesmos preceitos podem voltar a ser editados, desde que respeitadas as regras do processo legislativo.</p>
				
			</div>
			<div class="col s12 l6">
				
				<p>O desrespeito à <strong>reserva de iniciativa (Aula 2, seção 2.1.2)</strong> e a edição de <strong>medida provisória</strong> sobre matéria vedada a essa espécie normativa são outras hipóteses de inconstitucionalidade formal.</p>
				
				<p>Por fim, não se pode deixar de dizer que uma das causas mais frequentes de declaração de inconstitucionalidade formal pelo Supremo Tribunal Federal é o desrespeito às regras de distribuição de competência legislativa entre os entes federados previstas nos <strong>artigos 22 a 24 da Constituição</strong>. Em outras palavras, quando União, Estados, Distrito Federal e Municípios elaboram leis para tratar de matérias reservadas aos outros — por exemplo, uma lei estadual versando sobre telecomunicações, matéria de competência legislativa exclusiva da União, como no precedente a seguir.</p>
				
				<blockquote class="legislacao">
					
					<p>Ação direta de inconstitucionalidade. Lei nº 9.450, de 31 de janeiro de 2011, do Estado do Rio Grande do Norte, a qual veda a cobrança, no âmbito daquele Estado, das tarifas de assinatura básica pelas concessionárias prestadoras de serviços de telefonia fixa e móvel. Competência privativa da União para legislar sobre telecomunicações. Violação do art. 22, IV, da Constituição Federal. Precedentes. Inconstitucionalidade formal. Procedência da ação.</p>
					
					<p class="citacao">(<strong>ADI 4.603/RN</strong>, Relator Ministro Dias Toffoli, Tribunal Pleno, julgamento em 1º-7-2016, <em>DJe</em> 12-8-2016)</p>
					
				</blockquote>
				
