				<h4>2.3 Objeto</h4>
			</div>
			<div class="col s12 l7">
				
				<p>Que espécies de normas podem ser objeto da ação direta de inconstitucionalidade? Segundo o artigo 102, I, <em>a</em>, da Constituição Federal, a ADI pode ter como objeto a declaração de inconstitucionalidade de <strong>lei ou ato normativo federal ou estadual</strong>.</p>
				
				<p>A ADI 3.510 foi ajuizada contra uma lei, mais precisamente, contra o artigo 5º da Lei 11.105/2005 (Lei de Biossegurança).</p>
				
				<p>Já sabemos o que são leis, certo? Se não se recorda, basta voltar ao material da Aula 2 para relembrar. Podem ser objeto de ações diretas de inconstitucionalidade as leis ordinárias, leis complementares, leis delegadas e medidas provisórias.</p>
				
				<p>Agora, o que são atos normativos? Embora a questão não seja livre de controvérsias, podemos dizer, em linhas gerais, que atos normativos, para os fins da ação direta de inconstitucionalidade, são normas produzidas, com força de lei, por outros entes do Estado, que não o Poder Legislativo, para regular determinada situação em caráter geral e abstrato. Podem ser Resoluções, Decretos, Instruções Normativas, Normas Regulamentadoras etc. O Tribunal Superior Eleitoral, por exemplo, edita normas gerais, com força de lei, para regular as eleições. Agências reguladoras, como a ANATEL e a ANVISA, editam normas nas áreas de sua competência, mas nem todas as normas que editam podem ser objeto de ação direta de inconstitucionalidade.</p>
				
			</div>
			<div class="col s12 l5">
				
				<p>Os critérios utilizados pelo STF para decidir se determinado ato normativo pode ser alvo de ação direta de inconstitucionalidade são a generalidade e a abstração. É geral e abstrato um texto normativo que prevê uma hipótese geral para regular os fatos que nela se enquadrem, assim como faz uma lei.</p>
				
				<p>É um ato normativo passível de ataque por meio de ação direta de inconstitucionalidade, por exemplo, uma Resolução da ANVISA estabelecendo regras sobre as informações que devem constar em embalagens de medicamentos, a serem observadas por todos os fabricantes e distribuidores desses produtos.</p>
				
				<p>Não pode ser alvo de ação direta de inconstitucionalidade, contudo, uma Resolução da ANVISA que proíba as drogarias de comercializarem determinado lote de medicamentos que deixou de cumprir alguma regra (não importa se uma regra editada pela própria ANVISA ou uma lei federal). Nesse caso, o ato, ainda que formalmente seja normativo, não é considerado suficientemente geral e abstrato.</p>
				
