				<p>Chegamos à quarta aula do curso <strong>Introdução ao direito constitucional e ao controle de constitucionalidade</strong>!</p>
				
				<p>Na aula anterior, aprendemos sobre um dos principais conteúdos da Constituição, os direitos fundamentais, e os limites que representam à atuação do Estado.</p>
				
				<p>Agora vamos nos deter sobre um importante mecanismo para fazer valer esses direitos: a <strong>jurisdição constitucional</strong> e, em particular, o que chamamos de <strong>controle concentrado de constitucionalidade</strong>.</p>
				
				<p>Como já afirmamos na aula inaugural do curso, a jurisdição constitucional, ou controle de constitucionalidade, é a atividade exercida pelo Poder Judiciário, mediante a qual se afere a compatibilidade de uma lei, ou da sua aplicação em determinado caso, com o que diz a Constituição.</p>
				
				<p>Antes de prosseguirmos no exame do controle concentrado de constitucionalidade, vamos relembrar o que o caracteriza. Já vimos que a teoria constitucional tradicionalmente classifica os modelos de controle de constitucionalidade existentes conforme algumas de suas principais características. Desse modo, com base nessas características, o controle de constitucionalidade pode ser classificado como <strong>difuso</strong> ou <strong>concentrado</strong>.</p>
				
