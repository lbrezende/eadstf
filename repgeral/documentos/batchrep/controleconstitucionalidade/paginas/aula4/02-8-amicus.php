				<h4>2.8 <em>Amicus curiae</em></h4>
			</div>
			<div class="col s12 l6 offset-l3">
				
				<p>Em qualquer momento, antes da inclusão do processo na pauta do Plenário, com a liberação do relatório para os demais Ministros, pode o Ministro Relator admitir a participação de órgãos ou entidades que tenham informações técnicas para contribuir com o Tribunal. Esses participantes são conhecidos pela expressão latina <em>amicus curiae</em>, que significa <strong>amigo da Corte</strong>. A admissão do <em>amicus curiae</em> visa a aproximar o Supremo Tribunal dos diferentes pontos de vista existentes na sociedade, tornando a sua atuação mais democrática e mais embasada.</p>
				
				<p>Em razão da sua repercussão social, a ADI 3.510 contou com a participação, como amigos da Corte, de organizações ligadas à proteção e à promoção dos direitos humanos — Conectas Direitos Humanos; Centro de Direitos Humanos (CDH); Movimento em Prol da Vida (MOVITAE); Instituto de Bioética, Direitos Humanos e Gênero (ANIS) — e religiosas — Confederação Nacional dos Bispos do Brasil (CNBB). Essas entidades auxiliaram a Corte com a apresentação de argumentos, informações técnicas e pontos de vista diferenciados.</p>
				
