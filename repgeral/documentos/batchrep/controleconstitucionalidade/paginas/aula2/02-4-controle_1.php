				<h4>2.1.3 O controle preventivo de constitucionalidade exercido pelo Poder Legislativo</h4>
				<p>&nbsp;</p>
				
			</div>
			<div class="col s12 l6">
				
				<p>Esboçadas as linhas gerais da estrutura do Poder Legislativo e do processo legislativo, vejamos como esse Poder da República exerce parcela do controle preventivo de constitucionalidade.</p>
				
				<p>O processo legislativo tem início com a apresentação de projeto de lei, de projeto de lei complementar, de projeto de lei de conversão de medida provisória ou de proposta de emenda à Constituição na Câmara dos Deputados ou no Senado Federal, conforme o caso.</p>
				
				<p>Uma importante etapa desse processo é a análise das proposições, no interior das Casas legislativas, pelas comissões temáticas. Dentre elas, destacam-se pela relevância a Comissão de Constituição e Justiça e de Cidadania (CCJC), na Câmara dos Deputados, e a Comissão de Constituição, Justiça e Cidadania (CCJ), no Senado Federal, que, entre outros aspectos, examinam a constitucionalidade das matérias em trâmite.</p>
				
			</div>
			<div class="col s12 l6">
				
				<div class="box box-saiba-mais">
					<p>Cada uma das Casas do Congresso Nacional — Câmara dos Deputados e Senado Federal — possui um número de comissões temporárias e permanentes. É no âmbito das comissões que são estudadas mais detalhadamente e discutidas mais detidamente as propostas legislativas apresentadas. Mas, nem todas as comissões têm função propriamente legislativa. As Comissões Parlamentares de Inquérito (CPI), por exemplo, que aparecem com frequência nos noticiários, têm função investigativa.</p>
