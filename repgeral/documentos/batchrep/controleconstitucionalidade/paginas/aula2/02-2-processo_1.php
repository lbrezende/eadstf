				<h4>2.1.1 Processo legislativo</h4>
			</div>
			<div class="col s12 l6">
				<p>O processo legislativo compreende a elaboração de diferentes espécies de normas. Entre elas, as mais importantes são:</p>
				
				<ul class="setas1">
					<li>as leis ordinárias;</li>
					<li>as leis complementares;</li>
					<li>as medidas provisórias; e</li>
					<li>as emendas constitucionais.</li>
				</ul>
				
				<h5 class="simples">Lei ordinária</h5>
				
				<p>A grande maioria das <?php _el('glossario','relações jurídicas');?> na sociedade é regida por leis gerais e abstratas, que chamamos de ordinárias. <strong>Leis ordinárias</strong> são, simplesmente, leis não especiais. Correspondem à maioria das leis elaboradas, para as quais não se requer um rito especial de tramitação nem um quórum especial para aprovação. Basta a <strong>maioria simples</strong> nas duas Casas e, em seguida, a sanção presidencial.</p>
				
			</div>
			<div class="col s12 l6">
				
				<p>A <a href="https://goo.gl/1tyKS" class="ui-link" rel="external" target="_blank">Lei 7.783/1989</a>, que regula o exercício do direito de greve, a <a href="https://goo.gl/P7nUD" class="ui-link" rel="external" target="_blank">Lei 8.078/1990</a>, que estabelece o Código de Proteção e Defesa do Consumidor, e a <a href="https://goo.gl/lHseJ" class="ui-link" rel="external" target="_blank">Lei 11.343/2006</a>, que define crimes relacionados ao tráfico ilícito de entorpecentes, são exemplos de leis ordinárias.</p>
				
				<div class="box box-saiba-mais">
					<p>Por maioria simples entende-se o primeiro número inteiro superior à metade dos presentes, desde que alcançado o quórum mínimo de presenças para que uma lei seja votada. Já a maioria absoluta exige, para a aprovação, número de votos favoráveis igual ao primeiro número inteiro superior à metade dos membros da Casa.</p>
