				<h5>Emenda Constitucional</h5>
				
				<p>As emendas constitucionais, por sua vez, são especialmente delicadas, pois, como o próprio nome sugere, materializam alteração do próprio texto da Constituição.</p>
				
				<p>Aqui, é importante fazer uma observação. Quando falamos em alteração da Constituição, já não se trata mais do Poder Legislativo, mas, sim, do Poder Constituinte derivado. Falamos sobre ele na <strong>Aula 1</strong>, lembra-se?</p>
				
				<div class="box box-recordar">
					<p>Poder Constituinte derivado é o poder de revisão ou alteração de normas da Constituição. Não é mais um poder ilimitado, pois está vinculado à forma prevista na própria Constituição.</p>
				</div>
				
				<p>A proposta de emenda constitucional requer aprovação em dois turnos na Câmara dos Deputados e no Senado Federal, por pelo menos três quintos dos membros de cada Casa, e não depende de sanção presidencial. Além disso, somente podem ser objeto de emenda as normas da Constituição que não sejam consideradas <strong>cláusulas pétreas</strong> e, consequentemente, imutáveis.</p>
				
