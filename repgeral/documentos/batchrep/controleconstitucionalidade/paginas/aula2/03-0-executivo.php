				<h3>3. O Poder Executivo e o controle de constitucionalidade</h3>
				
			</div>
			<div class="col s12 l6">
				
				<p>O Poder Executivo, na medida em que participa do processo legislativo, também exerce o controle preventivo de constitucionalidade, notadamente, por meio do <strong>veto</strong>.</p>
				
				<p>Uma vez aprovado em ambas as Casas do Congresso Nacional, o projeto de lei ordinária, o projeto de lei complementar ou, ainda, o projeto de conversão de medida provisória com alteração do texto original é enviado ao Presidente da República para que o sancione ou vete.</p>
				
				<p>Por meio da <strong>sanção</strong>, o Presidente da República manifesta sua concordância com o conteúdo da medida legislativa aprovada pelo Congresso Nacional. Com a sanção, são completados todos os passos do processo legislativo, e, em seguida, é publicada a lei correspondente.</p>
				
				<p>Se, passados quinze dias desde o recebimento do projeto, o Presidente da República não se manifestar pela sanção nem pelo veto, ocorre a sanção tácita.</p>
				
			</div>
			<div class="col s12 l6">
				
				<p>Por outro lado, o Presidente poderá <strong>vetar</strong> total ou parcialmente o projeto, se considerá-lo, no todo ou em parte, <strong>inconstitucional</strong> ou contrário ao interesse público, devendo comunicar ao Presidente do Senado Federal os motivos do veto.</p>
				
				<p>O veto será apreciado pelo Congresso Nacional, em sessão conjunta, e somente poderá ser rejeitado pela maioria absoluta dos deputados e senadores.</p>
				
				<p>As considerações sobre o caráter muitas vezes político do juízo realizado pelas comissões parlamentares responsáveis pelo controle prévio de constitucionalidade dos projetos de medidas legislativas, feitas anteriormente nesta aula, certamente valem para o exercício do veto presidencial. Apesar disso, este é um mecanismo de controle efetivamente capaz de evitar a entrada de normas inconstitucionais no ordenamento jurídico.</p>
				
