						<p>No <strong>Senado Federal</strong>, por sua vez, os Estados e o Distrito Federal são representados, cada um, por três senadores, com mandato de oito anos. A cada quatro anos, a representação de cada Estado e do Distrito Federal é renovada, alternadamente, por um terço e dois terços das cadeiras. Como o Brasil é formado por 27 entidades federadas (26 Estados e o Distrito Federal), temos 81 Senadores.</p>
