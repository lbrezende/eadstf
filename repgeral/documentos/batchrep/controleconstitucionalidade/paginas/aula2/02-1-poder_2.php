				<p>A divisão do Poder Legislativo em duas casas, como acontece no Congresso Nacional brasileiro, é chamada de <strong>bicameralismo</strong>. A maioria dos países estruturados em forma de Federação adota legislaturas bicamerais. Em uma das Casas, a representação por ente federado observa a proporcionalidade das respectivas populações e, na outra, os entes federados têm o mesmo número de cadeiras.</p>
				
				<p>A justificativa clássica para essa prática é assegurar um espaço em que todos os Estados sejam iguais. Dessa forma, os Estados menores e menos populosos e, por isso mesmo, com menor representação proporcional, não seriam marginalizados no processo político pelos Estados maiores, que têm representação maior na outra casa.</p>
				
			</div>
			<div class="col s12 l5">
				<p>&nbsp;</p>
				<p>Logo, podemos dizer que a Câmara dos Deputados representa o povo e o Senado Federal representa os Estados e o Distrito Federal.</p>
				
				<p>Nos Estados e no Distrito Federal, ao contrário do que ocorre com a União, o Poder Legislativo funciona de forma <strong>unicameral</strong>, não sendo dividido em duas casas. Não há, nesse caso, um órgão equivalente ao Senado.</p>
				
				<p>Nos Estados, os representantes eleitos pelo povo reúnem-se em <strong>Assembleias Legislativas</strong> e, no Distrito Federal, na <strong>Câmara Legislativa</strong>.</p>
				
				<div class="box box-observe">
					<p>Como não há Municípios no Distrito Federal, um único órgão, a Câmara Legislativa, acumula as funções inerentes à Assembleia Legislativa e à Câmara Municipal.</p>
