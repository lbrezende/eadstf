				<h3>4. O Poder Judiciário e o controle de constitucionalidade</h3>
				
			</div>
			<div class="col s12 l7">
				
				<p>Se não existissem os mecanismos preventivos de controle de constitucionalidade desempenhados pelos Poderes Legislativo e Executivo no curso do processo legislativo, é seguro afirmar que número significativamente maior de leis inconstitucionais poderia vir a ser aprovado. Muitos projetos de lei inconstitucionais esbarram em pareceres contrários na CCJC da Câmara ou na CCJ do Senado ou, ainda, no veto presidencial.</p>
				
				<p>Contudo, se esses mecanismos prévios fossem absolutamente efetivos, não seria necessário o controle repressivo de constitucionalidade realizado <em>a posteriori</em> pelo Poder Judiciário.</p>
				
				<p>Como vimos, o caráter político dos Poderes Legislativo e Executivo pode comprometer a análise de constitucionalidade por eles desempenhada. Por essa razão, cabe, ainda, ao Poder Judiciário, em face do seu caráter técnico e do maior distanciamento em relação ao jogo político, dar a última palavra quanto à constitucionalidade das leis.</p>
				
			</div>
			<div class="col s12 l5">
				
				<p>Especialmente em relação ao controle difuso, a avaliação da constitucionalidade das leis é decorrência da própria atividade do Poder Judiciário de aplicar as leis às situações concretas que lhe são apresentadas, no contexto de um Estado constitucional.</p>
				
				<p>A lógica é a seguinte: ao julgar um determinado caso, o magistrado deve buscar a lei aplicável. Ao mesmo tempo, a decisão deve observar a Constituição, que é a Lei Maior, à qual a validade de todas as demais leis está subordinada. Se a aplicação da lei no caso se revela incompatível com a prevalência da Constituição, aquela lei deve ceder diante desta.</p>
				
