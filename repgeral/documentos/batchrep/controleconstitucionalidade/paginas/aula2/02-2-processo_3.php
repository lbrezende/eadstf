			<div class="col s12 l8 offset-l2">
				
				<p>As medidas provisórias configuram um caso especial de inversão da regra geral segundo a qual o processo legislativo tem início no <?php _el('glossario','Parlamento'); ?>. São normas com força de lei editadas pelo Presidente da República, que passam a valer imediatamente, embora com eficácia temporária (sessenta dias prorrogáveis por igual período). Devem ser, no entanto, submetidas ao Congresso Nacional para que sejam confirmadas, caso em que serão convertidas em lei ou rejeitadas.</p>
				
				<p>De acordo com a Constituição, a adoção de medidas provisórias depende da presença de dois requisitos: relevância e urgência. Contudo, há matérias sobre as quais é vedada a edição de medidas provisórias, como, por exemplo, direito penal e matérias reservadas a lei complementar.</p>
				
				<p>A Medida Provisória 195/2004, que obrigava todos os aparelhos de televisão comercializados no Brasil a conter dispositivo que permitisse ao usuário bloquear previamente a receptação de programas considerados inadequados, é um exemplo de medida provisória que não foi convertida em lei. Foi rejeitada pelo Congresso Nacional, que entendeu ausentes os pressupostos constitucionais de relevância e urgência, em um típico caso de controle preventivo exercido pelo Poder Legislativo, o que estudaremos logo adiante.</p>
				
				<p>Um exemplo de medida provisória convertida em lei é a Medida Provisória 665/2014, que tornou mais rígidas as exigências para acesso ao seguro-desemprego. Embora tenha sofrido algumas alterações, foi finalmente convertida na <a href="https://goo.gl/nHA7r8" class="ui-link" rel="external" target="_blank">Lei 13.134/2015</a>.</p>
				
				<p>Em comparação às demais espécies legislativas, o processo legislativo de conversão de medida provisória em lei é particularmente complexo. Caso queira se aprofundar no tema, sugiro a leitura do <?php _el('legis','artigo 62'); ?> da Constituição da República e seus doze parágrafos, que detalham diversos aspectos desse processo.</p>
				
