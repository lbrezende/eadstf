				<p>Bem-vindo à segunda aula do curso <strong>Introdução ao direito constitucional e ao controle de constitucionalidade</strong>!</p>
				
				<p>Na aula anterior, você ficou sabendo que, além de serem aprovadas democraticamente, as leis devem ser constitucionais. Aprendeu que o controle de constitucionalidade é o processo pelo qual se afere a compatibilidade das leis com a <?php _el('glossario','Constituição'); ?> da República. Viu também que essa é a atividade precípua do Supremo Tribunal Federal (STF), que a desempenha após as leis terem sido votadas, aprovadas e sancionadas. </p>
				
				<p>Nesse contexto, dizemos que o controle de constitucionalidade exercido pelo Poder Judiciário e, em especial pelo STF, é a <em>posteriori</em> ou repressivo, pois é exercido somente após iniciada a vigência das leis.</p>
				
				<p><strong><em>A posteriori</em></strong>: porque ocorre posteriormente à entrada das normas na <?php _el('glossario','ordem jurídica'); ?>.</p>
				
			</div>
			<div class="col s12 l6">
				
				<p><strong>Repressivo</strong>: porque tem como consequência a retirada, da ordem jurídica, de normas que nela já haviam entrado. Frequentemente, acontece de uma norma vir a ser declarada inconstitucional e, consequentemente, excluída da ordem jurídica após já ter regulado diversas situações e produzido uma série de efeitos.</p>
				
				<p>Entretanto, o controle exercido <em>a posteriori</em> e em caráter repressivo pelo Poder Judiciário não é o único mecanismo pelo qual a constitucionalidade das normas é analisada.</p>
				
				<p>A Constituição da República prevê outros mecanismos de controle, exercidos pelos Poderes Legislativo e Executivo, que operam ainda durante o processo legislativo. Esses mecanismos são chamados de preventivos ou <em>a priori</em> porque são destinados a evitar que uma lei inconstitucional venha a ser aprovada e entre em vigor. Esse assunto será o objeto do nosso estudo nesta aula. Vamos a ele, então?</p>
				
