				<p>Na história do constitucionalismo, o julgamento do caso <strong><em>Marbury v. Madison</em></strong> pela Suprema Corte dos Estados Unidos, em 1803, costuma ser apontado como referência da primeira vez em que a supremacia da Constituição foi invocada de modo a permitir que o Poder Judiciário afastasse a aplicação de uma lei tida por inconstitucional. Esse procedimento veio a ser chamado, no direito americano, de <em>judicial review</em> (revisão judicial).</p>
				
