				<p>O <em>judicial review</em> é o exame, pelo Poder Judiciário, da compatibilidade entre a lei em causa e a Constituição, e guarda estreita correspondência com o nosso controle difuso de constitucionalidade.</p>
				
			</div>
			<div class="col s12 l6">
				
				<p>Surgido inicialmente, portanto, apenas como controle difuso, a partir da noção de supremacia da Constituição o controle de constitucionalidade evoluiu e diversificou-se ao longo de mais de dois séculos de experiências nos regimes constitucionais democráticos.</p>
				
				<p>O modelo do controle abstrato de constitucionalidade, desenvolvido mais tarde, compartilha das premissas inauguradas pelo judicial review, de que o Poder Legislativo, por vezes, produz normas incompatíveis com a Constituição e de que compete à autoridade judicial, porque não diretamente envolvida na política, declarar a inconstitucionalidade dessas normas. Situa essa autoridade, porém, em uma Corte Constitucional dedicada a se manifestar sobre a constitucionalidade das leis.</p>
				
