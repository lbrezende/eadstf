				<h4>2.1.2 Iniciativa das leis</h4>
				
				<p>De modo geral, o processo legislativo, no tocante às leis ordinárias e complementares, pode ser iniciado por qualquer membro da Câmara dos Deputados ou do Senado Federal, pelo Presidente da República, pelo Supremo Tribunal Federal, pelos Tribunais Superiores, pelo Procurador-Geral da República e pelos cidadãos.</p>
				
				<p>Algumas matérias, todavia, estão sujeitas à chamada <strong>reserva de iniciativa</strong>. Isso significa que se excepciona, para esses casos, quem pode dar início ao processo de elaboração e aprovação de lei. Assim, nos termos da Constituição, somente o Presidente da República pode apresentar projeto de lei versando, por exemplo, sobre a fixação ou modificação dos efetivos das Forças Armadas ou sobre a aposentadoria dos servidores públicos da União. Já a lei complementar que venha a dispor sobre o Estatuto da Magistratura é de iniciativa do Supremo Tribunal Federal.</p>
				
