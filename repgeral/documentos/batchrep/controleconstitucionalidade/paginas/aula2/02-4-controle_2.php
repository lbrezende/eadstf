				<p>Na Câmara dos Deputados, o parecer da CCJC quanto à constitucionalidade da matéria submetida a deliberação tem caráter terminativo e prejudica a discussão ou votação de qualquer projeto semelhante a outro considerado inconstitucional pela Comissão. Igualmente, no Senado Federal, a manifestação da CCJ pela inconstitucionalidade de uma medida pode ser suficiente para o seu arquivamento.</p>
				
				<p>No caso das medidas provisórias, a Constituição determina, ainda, que os seus pressupostos constitucionais sejam examinados por uma comissão mista de deputados e senadores, que emitirá parecer antes da sua apreciação em cada uma das Casas do Congresso Nacional.</p>
				
				<p>Por um lado, não devemos ignorar o fato de que o juízo de valor efetuado em Comissões integradas por ocupantes de cargos eletivos assume, naturalmente, significativa feição política em possível detrimento de uma análise mais técnica. Por outro, cabe-nos reconhecer, ainda assim, o papel que desempenham na seleção das propostas legislativas que chegam a ser efetivamente votadas no Parlamento, consistindo em verdadeiro mecanismo de controle prévio de constitucionalidade de normas.</p>
				
				<p>O Poder Legislativo também exerce uma espécie de controle de constitucionalidade quando, mediante <strong>decreto legislativo</strong>, susta ato normativo do Poder Executivo que, no seu entendimento, exorbitou do poder regulamentar ou dos limites de delegação legislativa.</p>
				
