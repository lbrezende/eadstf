				<h5>Lei complementar</h5>
				
				<p>Para algumas matérias, a Constituição expressamente prevê a necessidade de uma <strong>lei complementar</strong>, assim chamada porque é destinada a complementar a orientação traçada nela própria para a regulação daquele tema.</p>
				
			</div>
		</div>
		<div class="row">
			<div class="col s12 m6 l5 push-l4">
				
				<p>Entre as matérias reservadas a lei complementar previstas na Constituição da República, podemos citar: a proteção da relação de emprego contra despedida arbitrária ou sem justa causa (art. 7º, I, da CR); a adoção de critérios especiais para a concessão de aposentadoria a servidores com deficiência ou que exerçam atividades de risco ou insalubres (art. 40, § 4º, da CR); o estatuto da magistratura (art. 93 da CR); as atribuições e o estatuto do Ministério Público (art. 128, § 5º, da CR); o procedimento para o processo judicial de desapropriação de imóvel rural, por interesse social, para fins de reforma agrária (art. 184, § 3º, da CR); e o regime de previdência complementar privada (art. 202 da CR), entre diversas outras.</p>
				
				<p>Para exemplificar, as atribuições do Ministério Público da União estão previstas na <a href="http://www.planalto.gov.br/ccivil_03/leis/lcp/lcp75.htm" class="ui-link" rel="external" target="_blank">Lei Complementar 75/1993</a>, e o procedimento de desapropriação, por interesse social, para fins de reforma agrária é objeto da <a href="http://www.planalto.gov.br/ccivil_03/leis/LCP/Lcp76.htm" class="ui-link" rel="external" target="_blank">Lei Complementar 76/1993</a>.</p>
				
				<p>As leis complementares precisam ser aprovadas pela <strong>maioria absoluta</strong> em cada Casa legislativa, antes de seguirem à sanção presidencial.</p>
				
