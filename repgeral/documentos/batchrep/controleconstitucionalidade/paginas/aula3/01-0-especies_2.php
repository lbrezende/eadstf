					<p>Com originalmente apenas sete artigos, a Constituição dos Estados Unidos, em vigor desde 1788, foi acrescida, até hoje, de somente 27 emendas, sendo um típico exemplo de constituição sintética.</p>
					<p>Em comparação, a Constituição da República Federativa do Brasil, promulgada em 1988, reúne 250 artigos e, com menos de trinta anos de vida, já conta com 91 emendas.</p>
					
				</div>
				
				<p>Essa dicotomia pode ser ilustrada tomando-se como exemplo o <strong><?php _el('legis','art. 7º, XIII');?>, da Constituição brasileira</strong>, que fixa em oito horas diárias e <strong>44</strong> horas semanais a duração máxima da jornada de trabalho normal. Como veremos adiante, a estipulação de um limite para a jornada de trabalho integra o rol dos direitos fundamentais sociais. Presente essa regra na Constituição, a previsão de uma jornada de trabalho normal com duração superior, seja mediante lei ou no contrato de trabalho, seria inconstitucional.</p>
				
				<p>Porém, ao mesmo tempo em que a constitucionalização torna o direito mais estável, dificultando a sua redução ou subtração, também dificulta a sua ampliação. Muito se discute a redução para quarenta ou até para 35 horas a duração máxima da jornada de trabalho. Entretanto, como a duração atual está prevista na Constituição, isso não poderia ser feito simplesmente por lei.</p>
				
