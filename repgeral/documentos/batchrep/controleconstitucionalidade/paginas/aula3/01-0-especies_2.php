				<p>Lembra-se do que vimos na aula passada sobre as propostas de emenda à Constituição? Por serem mais resistentes às tentativas de alteração, as normas constitucionais tendem a ser mais estáveis do que outros tipos de normas jurídicas, como, por exemplo, as leis. Essa característica pode se manifestar tanto como uma garantia contra retrocessos quanto como um obstáculo para avanços.</p>
				<p>Também é importante ressaltar que, como as constituições são criações do espírito humano, localizadas no tempo e no espaço, o caráter delas sofrerá forte influência do momento histórico em que promulgadas e das experiências vividas por um povo. Os dispositivos que condenam vigorosamente a prática da tortura, na Constituição do Brasil promulgada em 1988 (<strong>art. 5º, III e XLIII</strong>), são representativos do momento histórico da sua adoção, em que o país sinalizava a ruptura com o passado ditatorial.</p>
