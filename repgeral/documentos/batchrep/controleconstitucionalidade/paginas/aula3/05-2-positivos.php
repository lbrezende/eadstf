				<p>No caso das prestações normativas, o Estado é obrigado a produzir e manter uma legislação que vai interferir nas relações sociais ou econômicas para assegurar determinadas condições. Enquadra-se aí boa parte da legislação trabalhista, que, estabelecida pelo Estado, reduz a margem de liberdade contratual entre empregados e empregadores para garantir que certos padrões mínimos sejam respeitados, como salário mínimo (<?php _el('legis','art. 7º, IV, da CF'); ?>), jornada máxima (<?php _el('legis','art. 7º, XIII, da CF');?>), férias remuneradas (<?php _el('legis','art. 7º, XVII, da CF'); ?>) etc.</p>
				
