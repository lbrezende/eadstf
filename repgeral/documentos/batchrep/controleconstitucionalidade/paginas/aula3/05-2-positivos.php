				<h4>5.2 Direitos positivos</h4>
				
				<p>Os direitos positivos, ou direitos de <em>status positivus</em>, têm esse nome porque permitem que grupos ou indivíduos exijam uma <strong>atuação do Estado</strong>, atribuindo a ele uma <strong>obrigação de fazer</strong>. Em outras palavras, obrigam o Estado a realizar uma prestação, razão pela qual também são chamados de <strong>direitos prestacionais</strong>. Como essa obrigação visa, afinal, à melhoria da qualidade de vida dos indivíduos em uma sociedade, recebem ainda o nome de <strong>direitos sociais</strong>.</p>
				
				<p>O conteúdo dessa prestação pode ser <strong>material</strong> ou <strong>normativo</strong>. No primeiro caso, o Estado é obrigado a fornecer determinados bens ou serviços. São exemplos de direitos dessa natureza a educação, a saúde, a segurança pública, a previdência social (<?php _el('legis','art. 6º da CF');?>) e o seguro-desemprego (<?php _el('legis','art. 7º, II, da CF');?>).</p>
				
