				<p>Os direitos negativos, ou direitos de <em>status negativus</em>, manifestam pretensões de resistência à intervenção estatal. São assim chamados justamente porque impõem ao Estado obrigações de <strong>deixar de fazer algo</strong>. Também chamados de <strong>direitos de liberdade</strong> ou <strong>direitos de defesa</strong>, são, essencialmente, proibições, ordens de abstenção direcionadas ao Estado, que se vê, desse modo, limitado em suas possibilidades de atuação. Com isso, criam espaços para o exercício de <strong>liberdades</strong> tanto individuais quanto coletivas.</p>
