				<h3>2. Os direitos fundamentais — carta de direitos</h3>
			</div>
			<div class="col s12 l5">
			
				<p>Hoje vamos nos concentrar em um conjunto particularmente importante de normas da Constituição: os <strong>direitos fundamentais</strong>.</p>
				
				<p>A previsão de um amplo sistema de direitos fundamentais é um forte indicador acerca da vocação genuinamente democrática de uma constituição. No mundo contemporâneo, é quase impossível pensar em democracia e Estado de Direito sem que eles sejam assegurados.</p>
				
				<p><strong>Mas o que são, afinal, os direitos fundamentais e quais direitos podem ser assim chamados?</strong></p>
				
				<p>De modo geral, os direitos fundamentais são aqueles afirmados na Constituição como garantias dos indivíduos e da sociedade diante da força do Estado, seja por estabelecerem esferas de autonomia protegidas contra a ingerência do Estado, seja por definirem obrigações a serem satisfeitas pelo Estado em relação aos indivíduos, seja por assegurarem a participação dos cidadãos na condução da política.</p>
				
			</div>
			<div class="col s12 l7">
			
				<p>A Constituição da República Federativa do Brasil traz um elenco de direitos fundamentais no seu “<strong>Título II — Dos Direitos e Garantias Fundamentais</strong>”, que abrange notadamente os direitos individuais, coletivos, sociais e políticos. No contexto da Constituição brasileira, a expressão <strong>carta de direitos</strong> costuma ser usada para se referir especialmente ao <strong>art. 5º</strong>, que contempla uma extensa lista de direitos individuais e coletivos.</p>
				
				<div class="box box-destaque">
					<p>O conjunto dos direitos fundamentais em uma constituição também costuma ser chamado de <strong>carta de direitos</strong> (<em>bill of rights</em>).</p>
				</div>
			
				<p>Há consenso entre os estudiosos do direito constitucional brasileiro de que o Título II não esgota todos os direitos fundamentais previstos na nossa Constituição. Há direitos previstos em diversos outros capítulos que também são reconhecidos como direitos fundamentais. É o caso, entre outros, do <strong>direito ao meio ambiente ecologicamente equilibrado</strong>, previsto no <strong><?php _el('legis','art. 225'); ?></strong>.</p>
				
				
