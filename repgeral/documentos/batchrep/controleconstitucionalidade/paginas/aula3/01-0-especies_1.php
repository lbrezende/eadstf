				<p>Bem-vindo à terceira aula do curso <strong>Introdução ao direito constitucional e ao controle de constitucionalidade</strong>!</p>
				
				<p>Na aula anterior, examinamos as linhas gerais do processo legislativo, com ênfase nos diferentes mecanismos institucionais para o controle da sua constitucionalidade: mecanismos que visam a assegurar que o resultado do processo legislativo — a lei — seja compatível com as regras e os princípios da Constituição.</p>
				<p>As constituições podem contemplar normas, ou conjuntos de normas, de diferentes tipos. No caso da Constituição da República Federativa do Brasil, podemos identificar facilmente os seguintes grupos de normas: </p>
				<ol class="lista lista-alpha">
					<li>normas que organizam a estrutura do Estado, definindo as funções e os limites de atuação de cada um dos Poderes;</li>
					<li>normas que demarcam as relações entre os diferentes entes que compõem a Federação (União, Estados, Distrito Federal e Municípios);</li>
					<li>normas para disciplinar a arrecadação dos tributos pagos pelos contribuintes;</li>
					<li>parâmetros para a organização da economia e da sociedade; e</li>
					<li>normas que afirmam uma série de <strong>direitos</strong>, chamados de <strong>fundamentais</strong>.</li>
				</ol>
			</div>
