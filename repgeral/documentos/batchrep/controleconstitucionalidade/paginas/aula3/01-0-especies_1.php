				<p>Nem todas as constituições tratam necessariamente de todos esses temas, e, assim como há, pelo mundo, constituições que deixam de fora alguns deles, há também países cujas constituições dedicam capítulos inteiros a assuntos que, no Brasil, não são tratados na Constituição.</p>
				<p>No caso da Constituição brasileira, uma das críticas mais frequentes diz respeito ao extenso número de temas abordados. Em muitos deles, o constituinte desce às minúcias.</p>
				<p>Concordemos ou não com essa crítica, ela não expressa apenas uma preferência estilística por concisão. Incluir um assunto na Constituição significa, na prática, reduzir o espaço da deliberação política possível sobre ele e, quanto mais detalhado for o seu tratamento na Constituição, mais engessadas se tornam as possibilidades de inovações legislativas a respeito. Não é difícil perceber que essa nossa opção por uma <?php _el('glossario','constituição analítica'); ?> pode ser considerada, a depender do ponto de vista, tanto uma vantagem quanto uma desvantagem em relação a uma <?php _el('glossario','constituição sintética'); ?>.</p>
				
