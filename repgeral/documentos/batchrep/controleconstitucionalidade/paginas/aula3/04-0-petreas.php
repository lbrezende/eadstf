				<h3>4. Direitos fundamentais e cláusulas pétreas</h3>
			</div>
			<div class="col s12 l10 offset-l1">
				<p>Na primeira aula, vimos que há normas constitucionais, as chamadas <strong>cláusulas pétreas</strong>, que não podem sequer ser objeto de deliberação mediante apresentação de proposta de emenda e, portanto, não podem ser alteradas. Entre as cláusulas pétreas, relacionadas no <strong><?php _el('legis','art. 60, § 4º'); ?>, da Constituição</strong>, estão expressamente incluídos “os direitos e as garantias individuais”. Esse preceito comporta diferentes interpretações e é motivo de controvérsia. As possibilidades seriam, da mais restrita para a mais abrangente:</p>
			</div>
			<div class="col s12 l10 offset-l1">
				<div class="box box-complementar box-simples">
					<blockquote>
						<p><strong>i.</strong> os incisos do art. 5º da CF que afirmam especificamente direitos fundamentais individuais são cláusulas pétreas;</p>
						<p><strong>ii.</strong>. os incisos do art. 5º da CF que afirmam especificamente direitos fundamentais individuais, assim como outros direitos individuais previstos na Constituição, são cláusulas pétreas;</p>
						<p><strong>iii.</strong> todos os incisos do art. 5º da CF que afirmam direitos fundamentais, sejam individuais ou coletivos, são cláusulas pétreas;</p>
						<p><strong>iv.</strong> todos os incisos do art. 5º da CF que afirmam direitos fundamentais, sejam individuais ou coletivos, assim como outros direitos fundamentais previstos na Constituição, são cláusulas pétreas;</p>
						<p><strong>v.</strong> todos os direitos fundamentais previstos no Título II da Constituição são cláusulas pétreas;</p>
						<p><strong>vi.</strong> todos os direitos fundamentais, de qualquer natureza, previstos em qualquer lugar da Constituição, são cláusulas pétreas.</p>
					</blockquote>
				</div>
			</div>
			<div class="col s12 l10 offset-l1">
				<p>Seja qual for a leitura prevalecente, não se pode negar que, no mínimo, os direitos e as garantias fundamentais de cunho individual são cláusulas pétreas e, por isso, não podem ser excluídos da Constituição nem mesmo por emenda constitucional.</p>
				
