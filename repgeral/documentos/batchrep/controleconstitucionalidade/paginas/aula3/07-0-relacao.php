				<p>O tema é polêmico e teóricos respeitados têm oferecido diferentes propostas para a sua solução. Eis algumas delas.</p>
				
				<p>Há quem postule uma hierarquia rígida entre direitos, de modo que prevalecerá, em caso de conflito, o direito que for hierarquicamente superior. Assim, no conflito entre dois direitos, como, por exemplo, o direito à liberdade de expressão e o direito à honra, prevalecerá sempre o mesmo direito. </p>
				
			</div>
			<div class="col s12 l6">
				
				<p>Há quem defenda uma hierarquia flexível, que deverá ser aferida no exame da situação concreta, para se decidir, naquele caso, qual é o direito mais importante e que será preservado, sacrificando-se o outro. Para usar o mesmo exemplo anterior, aqui a prevalência do direito à liberdade de expressão ou do direito à honra dependerá de uma valoração sobre qual dos direitos vale a pena preservar no caso específico.</p>
				
				<p>Há quem procure estabelecer diferentes técnicas para que se busque preservar, em cada caso, tanto quanto possível, os diferentes direitos fundamentais em jogo.</p>
				
