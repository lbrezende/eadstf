				<p>Por fim, vamos chamar a atenção para um dos tópicos mais discutidos da teoria constitucional, o que examina as relações, por vezes conflituosas, entre diferentes direitos fundamentais, em especial nos casos em que o respeito a um direito parece contradizer outro igualmente fundamental. Como resolver esses casos?</p>
				
