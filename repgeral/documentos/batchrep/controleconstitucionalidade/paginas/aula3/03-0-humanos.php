				<p>Já quando falamos em <strong>direitos fundamentais</strong>, estamos nos referindo a direitos cujo status diferenciado, assim como sua exigibilidade, decorre da sua previsão em um sistema jurídico constitucional, independentemente de considerações filosóficas ou de outra natureza.</p>
				
				<p>Assim, para ficarmos em poucos exemplos, liberdade religiosa e direito à educação são <strong>direitos humanos</strong> porque são reconhecidos como tais nos instrumentos internacionais mencionados e, no Brasil, também são <strong>direitos fundamentais</strong> porque são assim previstos nos <strong>arts. 5º e 6º da Constituição</strong>, respectivamente.</p>
				
				<p>Além disso, o conteúdo de um mesmo direito tido como fundamental pode assumir feições marcadamente diferentes em jurisdições distintas. É o caso do direito à <strong>liberdade de expressão</strong>, que, nos Estados Unidos, protege, segundo tem sido interpretada a Constituição daquele país, uma variedade de formas de expressão que não encontram proteção, por exemplo, na França.</p>
				
