					<p>Com quase setenta anos de idade, a Declaração Universal dos Direitos Humanos hoje tem o seu próprio website em língua portuguesa, produzido pela Organização das Nações Unidas. <a href="http://www.dudh.org.br/" class="ui-link" rel="external" target="_blank">Clique aqui</a> para navegar pelo texto da Declaração, conhecer a sua história e aprender como denunciar violações de direitos humanos.</p>
					
