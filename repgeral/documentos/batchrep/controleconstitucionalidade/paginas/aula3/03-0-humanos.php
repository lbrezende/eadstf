				<p>Se olharmos para o seu conteúdo, veremos que boa parte dos direitos considerados fundamentais corresponde, de fato, ao que conhecemos como direitos humanos. Entretanto, há diferenças técnicas e filosóficas importantes.</p>
				
				<p>O conceito de <strong>direitos humanos</strong> está fundado na ideia de que determinados direitos são <strong>universais</strong>, válidos para toda a humanidade independentemente das diferenças culturais entre os povos, e, uma vez inerentes à própria condição humana, preexistentes a qualquer forma de organização político-jurídica. Sua origem remonta às diversas teorias que afirmam a existência dos <strong>direitos naturais</strong>, tenham sido eles atribuídos por Deus ou deduzidos pela razão.</p>
				
				<p>No plano internacional, a célebre <a href="files/aula3/declaracao_universal_direitos_humanos.pdf" class="ui-link" rel="external" target="_blank">Declaração Universal dos Direitos Humanos</a>, adotada pela Assembleia Geral das Nações Unidas em <strong>1948</strong>, compõe, juntamente com o <a href="http://www.planalto.gov.br/ccivil_03/decreto/1990-1994/d0592.htm" class="ui-link" rel="external" target="_blank">Pacto Internacional dos Direitos Civis e Políticos</a> e o <a href="http://www.planalto.gov.br/ccivil_03/decreto/1990-1994/d0591.htm" class="ui-link" rel="external" target="_blank">Pacto Internacional dos Direitos Econômicos, Sociais e Culturais</a> (ambos de 1966), a chamada <a href="files/aula3/carta_internacional_direitos_humanos.pdf" class="ui-link" rel="external" target="_blank">Carta Internacional dos Direitos Humanos</a>.</p>
				
