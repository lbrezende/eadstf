				<h4>5.5 Considerações adicionais</h4>
				
				<p>Como afirmado, nenhuma dessas classificações é isenta de críticas e tampouco deve ser apreciada com excessiva rigidez. Sua finalidade é auxiliar na compreensão e na reflexão acerca do significado e do alcance dos direitos fundamentais, e não apenas os separar em caixas diferentes.</p>
				
				<p>A tentativa de estabelecer hierarquias entre direitos fundamentais costuma ser uma tarefa árdua e inglória.</p>
				
				<p>Assim, se, por um lado, a aplicação de determinados direitos sociais necessariamente fixa limites para o exercício de certas liberdades, por outro, assegura a satisfação de necessidades sem as quais não seria possível o exercício dessas mesmas liberdades.</p>
				
				<figure class="fig">
					<img src="img/aula3/social_individual.png" alt="" role="presentation" style="max-width: 440px;">
				</figure>
				
				<p>Para complicar um pouco, vale observar que direitos tipicamente negativos (liberdades) também têm um lado positivo (prestacional). O direito de propriedade, na prática, não se resume à restrição ao poder do Estado de interferir na propriedade particular. Ele seria letra morta não fosse a existência de todo um aparato estatal que inclui serviços como polícia e tribunais, por meio dos quais esse direito pode ser garantido contra terceiros. Embora seja um direito essencialmente contra o Estado, não deixa de depender do Estado para sua concretização.</p>
				
