				<h4>5.4 Direitos substantivos e procedimentais</h4>
				
				<p>Outra classificação bastante útil é a que separa os direitos fundamentais em <strong>direitos substantivos</strong> e <strong>direitos procedimentais</strong>.</p>
				
				<p>Direitos fundamentais substantivos são <strong>fins em si mesmos</strong>; direitos que satisfazem necessidades concretas dos indivíduos. São condições que, se não forem satisfeitas, prejudicam ou até mesmo impedem a capacidade individual de perseguir e realizar os próprios projetos de vida. Sob esse prisma, a privação de direitos como educação, saúde ou alimentação é equivalente à negação de direitos como a liberdade de expressão, a liberdade religiosa e a livre-iniciativa.</p>
				
				<p>Os direitos fundamentais procedimentais, por sua vez, consistem em mecanismos necessários em um regime de direitos para que os direitos substantivos tenham efetividade. Não são fins em si mesmos, mas, sim, <strong>meios</strong> para se chegar aos direitos-fim. Não faz sentido invocar, por exemplo, o direito ao devido processo legal se não for para preservar algum outro direito. Por outro lado, um regime que não assegure o devido processo legal jamais oferecerá proteção suficiente ao direito de propriedade ou ao direito de ir e vir.</p>
				
				<p>Considerando que a participação política é uma ferramenta dos indivíduos para fazer com que seus interesses sejam levados em consideração, em alguma medida, na tomada de decisão política, os direitos de participação política se encaixariam nessa definição de direitos procedimentais.</p>
				
