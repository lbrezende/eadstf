				<h4>2.1&nbsp; Formação do Estado Federal</h4>
				
				<p>Historicamente, as federações são constituídas de diferentes maneiras.</p>
				<p>Uma delas é por meio de um pacto entre unidades políticas separadas, como estados, que abrem mão da própria soberania para se unir sob um único ente político soberano. É o caso da formação dos Estados Unidos da América e da Austrália.</p>
				<p>A Áustria e o Brasil, por outro lado, são federações formadas a partir da divisão de um Estado unitário. Esse processo de formação, que podemos chamar de federalismo “de cima para baixo”, sem dúvida deixou marcas no nosso modelo. Em federações formadas dessa maneira, os estados federados em geral têm menos autonomia, ao passo que os governos federais tendem a concentrar mais poderes do que nas federações originadas da reunião de unidades independentes (“de baixo para cima”).</p>
				
