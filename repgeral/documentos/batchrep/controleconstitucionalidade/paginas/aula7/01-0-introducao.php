				<h2>Aula 7 <i>&mdash;</i> Controle de constitucionalidade e Federalismo</h2>
				<h3>1. O Federalismo — introdução</h3>
			</div>
			<div class="col s12 l4">
				
				<p>Aproximando-nos do final da nossa jornada, chegamos à sétima aula do nosso curso, na qual vamos abordar um dos mais vastos tópicos do direito constitucional moderno: o <strong>federalismo</strong>.</p>
				
				<p>Ao lermos o <strong>artigo 1º da Constituição</strong>, vemos que o Brasil é constituído como uma República Federativa, formada pela união indissolúvel dos estados, dos municípios e do Distrito Federal. Em outras palavras, o Brasil é uma espécie de federação.</p>
				
				<p>O que é, então, o federalismo? Trata-se de um conceito fundamental da teoria política e constitucional contemporânea, presente em diversos países do mundo, incluindo o Brasil. Federalismo é a divisão espacial do poder político entre um governo central e governos regionais.</p>
				
			</div>
			<div class="col s12 l4">
				
				<p>Dito dessa maneira até parece simples, mas é no momento de definir os poderes do governo central e os poderes dos governos regionais que a complexidade do tema aparece. Antes de prosseguir, é bom notar que as questões envolvendo o federalismo são campo fértil para o afloramento de paixões e ideologias políticas. Há tanto defensores ardorosos da autonomia dos entes regionais e da valorização das peculiaridades locais quanto quem aposte no fortalecimento do poder central como a melhor fórmula de se construir um país forte e uma governança eficiente. De fato, é preciso reconhecer que não deixam de existir bons argumentos para suportar ambos os lados desse debate.</p>
				
			</div>
			<div class="col s12 l4">
				
				<p>O federalismo também pode ser compreendido e estudado como uma modalidade de separação de poderes com o objetivo de evitar a sua concentração excessiva. Assim como estudamos a <strong>divisão funcional</strong> do poder político entre Executivo, Legislativo e Judiciário, o federalismo estabelece a <strong>divisão espacial</strong> do poder entre esferas distintas de governo. Desse modo, os poderes tanto dos ocupantes do governo nacional quanto dos grupos que dominarem o poder local serão sempre limitados.</p>
				
				<p>Neste curso, não temos a intenção de nos comprometer com a defesa de um ou outro ponto de vista. Nossos objetivos são expor de forma compreensível os conceitos teóricos pertinentes ao federalismo e descrever as linhas gerais do atual modelo de federalismo brasileiro, tal como estruturado pela Constituição de 1988.</p>
				
