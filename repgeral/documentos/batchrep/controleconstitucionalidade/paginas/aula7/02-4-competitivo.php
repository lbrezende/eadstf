				<h4>2.4&nbsp; Federalismo competitivo ou dual e federalismo cooperativo</h4>
			</div>
			<div class="col s12 l5">
				
				<p>O grau de integração entre as partes integrantes de uma federação, bem como a intensidade da relação entre elas e o governo federal, varia bastante dentro de um espectro contínuo que tem, de um lado, a ideia de federalismo competitivo e, de outro, o conceito de federalismo cooperativo.</p>
				
				<p>Os modelos em que os entes federados conservam ampla liberdade de autodeterminação, com poucos poderes investidos no governo federal, costumam ser chamados de <strong>federalismos duais</strong> ou <strong>competitivos</strong>. No limite, o governo federal seria um mero árbitro para solucionar eventuais conflitos entre os estados federados. O fato de, nos EUA, o consumo da maconha ser legalizado em alguns estados e crime em outros sugere a presença de elementos desse tipo de federalismo.</p>
				
				<p>Já no <strong>federalismo cooperativo</strong>, os entes que compõem a federação são orientados a cooperar com o governo federal para alcançar objetivos nacionais compartilhados e, em consequência, têm sua autonomia reduzida.</p>
				
			</div>
			<div class="col s12 l7">
				
				<p>Ao afirmar que entre os objetivos fundamentais da República Federativa do Brasil estão o “desenvolvimento nacional” e “reduzir as desigualdades regionais”, a <strong>Constituição</strong> aponta, em seu <strong>artigo 3º, incisos II e III</strong>, para um comprometimento, em certa medida, com a ideia do federalismo cooperativo. Também sinalizam nesse sentido a extensa lista de competências privativas da União, que veremos adiante, e a previsão do artigo 23, parágrafo único: “Leis complementares fixarão normas para a cooperação entre a União e os Estados, o Distrito Federal e os Municípios, tendo em vista o equilíbrio do desenvolvimento e do bem-estar em âmbito nacional”.</p>
				
				<p>Outro indicativo de um federalismo cooperativo são os repasses aos entes federados de recursos arrecadados pelo governo federal e a sua vinculação a finalidades predefinidas em nível nacional. No Brasil, estados e municípios não têm autonomia para definir percentuais mínimos de gastos com educação ou saúde, por exemplo.</p>
				
				<p>Na prática, dificilmente é possível qualificar um Estado Federal como puramente competitivo ou cooperativo. O que podemos fazer é, com base na análise de suas características, aferir, num determinado momento, a direção para a qual tendem em relação às extremidades.</p>
				
