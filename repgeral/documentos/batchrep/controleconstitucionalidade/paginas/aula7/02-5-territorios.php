				<h4>2.5 Territórios federais</h4>
			</div>
			<div class="col s12 l6">
				
				<p>Territórios federais são porções territoriais que não fazem parte de nenhum dos estados da Federação, sob controle direto do governo federal. Embora pertençam à Federação, não são entes federados autônomos. Dessa forma, não desfrutam, em geral, da autonomia que os estados conservam em relação ao poder central.</p>
				
			</div>
			<div class="col s12 l6">
				
				<p>Embora mencionados diversas vezes na Constituição, atualmente não há territórios federais no Brasil.</p>
				
				<p>Com a promulgação da Constituição de 1988, os então territórios federais do Amapá e de Roraima foram transformados em estados federados e o Território Federal de Fernando de Noronha foi incorporado ao estado de Pernambuco.</p>
				
			</div>
			<div class="col s12">
				
				<div class="box box-saiba-mais">
					
					<div class="row">
						<div class="col s12 l5">
							
							<p>Localizado em ponto estratégico do Atlântico Sul, o <strong>Território Federal de Fernando de Noronha</strong> foi criado em 1942, durante a Segunda Guerra Mundial, pelo desmembramento do estado de Pernambuco. No ano seguinte, mais cinco territórios federais foram criados em regiões fronteiriças estratégicas: Amapá, Guaporé, Iguaçu, Ponta-Porã e Rio Branco.</p>
							
							<p>O <strong>Território Federal do Guaporé</strong> foi criado a partir de desmembramentos dos estados do Amazonas e do Mato Grosso. Em 1956, seu nome foi alterado para <strong>Território Federal de Rondônia</strong> e, em 1982, foi transformado no <strong>estado de Rondônia</strong>.</p>
							
						</div>
						<div class="col s12 l7">
							
							<p>O <strong>Território Federal do Iguaçu</strong>, que ocupava a porção oeste do Paraná e de Santa Catarina, e o <strong>Território Federal de Ponta-Porã</strong>, em região do então estado de Mato Grosso (hoje faz parte do estado de Mato Grosso do Sul), foram extintos já em 1946, com a reincorporação das áreas por eles ocupadas aos estados.</p>
							
							<p>A área onde hoje está o estado de Roraima foi desmembrada do estado do Amazonas para a criação do Território Federal do Rio Branco. A partir de 1962, o território passou ser denominado Território Federal de Roraima e, em 1988, tornou-se o estado de Roraima.</p>
							
							<p>Por fim, desmembrado do estado do Pará, o <strong>Território Federal do Amapá</strong> passou a ser, desde 1988, o estado do Amapá.</p>
							
