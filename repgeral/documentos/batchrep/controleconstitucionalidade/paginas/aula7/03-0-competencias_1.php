				<h3>3. As competências constitucionais dos entes federativos</h3>
			</div>
			<div class="col s12 l7">
				
				<p>Quais são, na Federação brasileira, os poderes e as funções reservados à União, aos estados e aos municípios? Quais são os poderes por eles compartilhados? Esses temas, que dizem respeito à organização do Estado brasileiro, são objeto dos artigos 18 a 35 da Constituição. Em razão da extensão da matéria, vamos nos concentrar nas linhas gerais da distribuição de competências entre os entes da Federação.</p>
				
				<p>Em comparação com os modelos presentes nas constituições anteriores, a arquitetura da Federação delineada na Constituição brasileira de 1988 representou um razoável incremento na autonomia dos estados. Ainda assim, é fácil perceber que a União continua ocupando, no nosso sistema, posição de proeminência em relação a estados e municípios, no que se refere à concentração de poderes e competências.</p>
				
				<p>Entre as competências exclusivas da União (<strong>artigo 21</strong>), podemos destacar: manter relações com Estados estrangeiros, participar de organizações internacionais (como a ONU e o Mercosul), declarar guerra, emitir moeda e regular os serviços de telecomunicações e de geração e fornecimento de energia elétrica, o transporte aéreo e a exploração de material nuclear.</p>
				
			</div>
			<div class="col s12 l5">
				
				<p>Além disso, há um extenso rol de matérias sobre as quais somente a União pode editar leis (<strong>artigo 22</strong>), que abrange direito civil, comercial, penal, eleitoral e do trabalho, regime de exploração dos portos e navegação, trânsito e transporte, populações indígenas, imigração. Diferentemente do que acontece, por exemplo, nos Estados Unidos, os estados brasileiros não podem definir o que consideram crime ou como regular os contratos de trabalho, devendo observar a legislação da União.</p>
				
				<p>Por outro lado, a responsabilidade pela proteção do meio ambiente, a preservação das florestas, da fauna e da flora, dos monumentos e paisagens naturais e dos sítios arqueológicos é compartilhada pela União, pelos estados, pelos municípios e pelo Distrito Federal (<strong>artigo 23</strong>). Todos eles também podem instituir e arrecadar tributos (<strong>artigos 24, I, </strong>e<strong> 30, III</strong>).</p>
				
