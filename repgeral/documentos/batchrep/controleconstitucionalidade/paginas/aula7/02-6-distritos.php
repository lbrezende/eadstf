				<h4>2.6 Distritos Federais</h4>
			</div>
			<div class="col s12 l6">
				
				<p>Nos Estados Unidos e na Venezuela, o território onde fica a sede do governo federal (capital federal) é chamado de distrito federal, no sentido de que se trata de um território que não faz parte de nenhum estado, subordinado diretamente ao governo federal.</p>
				
				<p>As capitais federais do Brasil, da Argentina e do México também estão situadas em distritos federais, mas, nesses países, o distrito federal constitui um ente político separado que, embora seja estruturalmente diferente dos estados, integra a Federação.</p>
				
				<p>No Brasil, o Distrito Federal tem o seu próprio governo. A população do Distrito Federal elege o seu governador (que, na prática, também atua como uma espécie de “prefeito de Brasília”), bem como os membros da Câmara Legislativa, que acumula as competências das Assembleias Legislativas (estados) e das Câmaras Municipais (municípios).</p>
				
