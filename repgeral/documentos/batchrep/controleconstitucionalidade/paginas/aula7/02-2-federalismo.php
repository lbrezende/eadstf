				<h4>2.2&nbsp; Federalismo simétrico e federalismo assimétrico</h4>
				
				<p>A Constituição do Brasil trata todos os estados de modo igual. Todos têm o mesmo nível de autonomia e seus governos têm os mesmos poderes. Portanto, o federalismo brasileiro é simétrico.</p>
				<p>O federalismo assimétrico ocorre em federações em que os diferentes estados têm, segundo a própria Constituição, diferentes poderes e níveis de autonomia. Na Malásia, por exemplo, uma federação de treze estados, dois deles têm autonomia significativamente maior do que os demais.</p>
				
