				<h3>4. O federalismo brasileiro e o STF</h3>
				
				<p>A estrutura da Federação brasileira e, em particular, a distribuição de competências entre os entes federativos, estão constantemente na pauta do Supremo Tribunal Federal. Não são nada raros os questionamentos, perante o STF, acerca da constitucionalidade de leis sob a alegação de que teriam invadido a esfera de competência de outro ente da Federação.</p>
				
				<p>Além disso, compete ao STF julgar os conflitos entre a União e os estados, entre a União e o Distrito Federal, ou entre uns e outros.</p>
				
