				<p>A Constituição determinou que determinadas matérias podem simultaneamente ser objeto de legislação tanto da União quanto dos estados ou do Distrito Federal. É o que se chama de <strong>competência legislativa concorrente</strong>. Assim, a existência do Código de Defesa do Consumidor (legislação federal) não impede que os estados o complementem para atender a peculiaridades locais. Nesse exemplo, as relações de consumo ficam sujeitas à observância tanto da legislação federal (Código de Defesa do Consumidor) quanto da legislação estadual ou distrital que venha a suplementá-la (<strong>artigo 24</strong>).</p>
				
				<p>Aos estados foi concedida uma competência denominada <strong>residual</strong>. Isso quer dizer que a eles foram reservadas todas as competências não expressamente vedadas pela Constituição. Esse tipo de construção faz mais sentido em constituições que reservam à União um universo restrito de competências privativas. No caso da Constituição brasileira de 1988, acabou não tendo a funcionalidade esperada, uma vez que foi concentrado na União vasto leque de competências privativas, não sobrando muita coisa para ser enquadrada na competência residual dos estados.</p>
				
				<p>Os serviços de transporte coletivo urbano e o ensino fundamental são algumas das responsabilidades dos municípios. A eles também compete legislar sobre assuntos de interesse local que não lhes sejam vedados, inclusive com o objetivo de suplementar a legislação federal e estadual, quando necessário (<strong>artigo 30</strong>).</p>
				
