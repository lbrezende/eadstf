				<h3>2. Democracia</h3>
				<figure class="fig fig-left" aria-hidden="true" style="max-width: 406px;">
					<img src="img/aula1/Winston.png" alt="">
					<figcaption class="screen-reader-text">Winston Churchill (1874-1965), Primeiro-Ministro do Reino Unido durante a Segunda Guerra Mundial, foi um notável orador e estadista, forte crítico do nazismo e defensor da democracia parlamentar.</figcaption>
				</figure>
				
				<p>Um dos maiores líderes do século XX, <a href="https://goo.gl/n2QwF2" rel="external" target="_blank">Winston Churchill</a>, descreveu a democracia como “a pior forma de governo, salvo todas as demais formas que têm sido experimentadas de tempos em tempos”.</p>
				
				<p>Em linhas bem gerais, um Estado é democrático quando as normas que o governam são determinadas pelo povo, diretamente (democracia direta) ou por meio de representantes eleitos (democracia indireta ou parlamentar). Em outras palavras, quando o povo é o autor das leis, os governados são, em última análise, os próprios governantes.</p>
				
				<figure class="fig fig-right" aria-hidden="true" >
					<img src="img/aula1/Lincon.png" alt=""style="max-width: 406px;">
					<figcaption class="screen-reader-text">Décimo sexto Presidente dos Estados Unidos, Abraham Lincoln liderou aquele país durante sua maior crise, a Guerra Civil Americana, da qual saiu vitorioso com a preservação da União e a abolição da escravidão. Foi assassinado, ainda no exercício da Presidência, enquanto assistia a uma peça de teatro.</figcaption>
				</figure>
				
				<p>Tradicionalmente, portanto, a democracia é definida, a teor do célebre pronunciamento de <a href="https://goo.gl/4VA6F9" rel="external" target="_blank">Abraham Lincoln</a>, como o “governo do povo e para o povo”. Embutidos nessa definição, aparentemente singela, estão séculos de estudos teóricos e experiências práticas em torno da ideia de <strong>soberania popular</strong>.</p>
				
