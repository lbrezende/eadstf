						<p>Na mesma linha, o professor <strong>John Hart Ely</strong> não vê as restrições constitucionais como antidemocráticas, e, sim, como reforços da democracia. Isso porque esses limites asseguram a manutenção das condições para o exercício da democracia. O Tribunal constitucional, assim, é o guardião não só da Constituição, mas também da própria democracia.</p>
						
					</div>
