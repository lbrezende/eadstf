				<p>São as limitações constitucionais ao poder político que permitem que a democracia funcione, supere o princípio majoritário e seja o governo não só da maioria, mas de todos. É o princípio do constitucionalismo que, ao inserir uma carta de direitos fundamentais na base da ordem jurídica, assegura que maiorias de conveniência não subjuguem as minorias. É a função contramajoritária da Constituição que, ao limitar a democracia, permite que a democracia exista, sem se degenerar em despotismo. A Constituição tem esse papel de incluir no processo democrático consideração pelos interesses minoritários, de índios, homossexuais, quilombolas, minorias étnicas e religiosas etc.</p>
				<p>Há uma permanente tensão entre o princípio democrático e o constitucional.</p>
