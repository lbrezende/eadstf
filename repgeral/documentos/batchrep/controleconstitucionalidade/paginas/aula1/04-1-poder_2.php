						<p>Art. 60. A Constituição poderá ser emendada mediante proposta:</p>
						
						<p>I — de um terço, no mínimo, dos membros da Câmara dos Deputados ou do Senado Federal;</p>
						<p>II — do Presidente da República;</p>
						<p>III — de mais da metade das Assembleias Legislativas das unidades da Federação, manifestando-se, cada uma delas, pela maioria relativa de seus membros.</p>
						<p>§ 1º A Constituição não poderá ser emendada na vigência de intervenção federal, de estado de defesa ou de estado de sítio.</p>
						<p>§ 2º A proposta será discutida e votada em cada Casa do Congresso Nacional, em dois turnos, considerando-se aprovada se obtiver, em ambos, três quintos dos votos dos respectivos membros.</p>
						<p>§ 3º A emenda à Constituição será promulgada pelas Mesas da Câmara dos Deputados e do Senado Federal, com o respectivo número de ordem.</p>
						<p>§ 4º Não será objeto de deliberação a proposta de emenda tendente a abolir:</p>
						<p>I — a forma federativa de Estado;</p>
						<p>II — o voto direto, secreto, universal e periódico;</p>
						<p>III — a separação dos Poderes;</p>
						<p>IV — os direitos e garantias individuais.</p>
						<p>§ 5º A matéria constante de proposta de emenda rejeitada ou havida por prejudicada não pode ser objeto de nova proposta na mesma sessão legislativa.</p>
						
