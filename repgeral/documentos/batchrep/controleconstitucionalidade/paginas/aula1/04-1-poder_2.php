				<p>&nbsp;</p>
				
				<p>Na Constituição brasileira, o poder constituinte derivado é exercido pelo procedimento da proposta de emenda à Constituição, previsto no <strong>art. 60</strong>. A proposta de emenda à Constituição precisa ser aprovada na Câmara dos Deputados e no Senado, em dois turnos, por pelo menos três quintos dos membros de cada Casa. Além disso, nem todas as normas da Constituição podem ser objeto de deliberação pelo Poder Constituinte derivado. Às normas constitucionais que não podem ser alteradas, nem mesmo por emenda à Constituição, dá-se o nome de <strong>cláusulas pétreas</strong>.</p>
				
				<p>Alguns autores falam, ainda, no conceito de <strong>momento constituinte</strong>, para aludir aos momentos históricos em que, por uma especial conjuntura política e social, o Poder Constituinte originário se manifesta. São os momentos especialmente simbólicos de transição política, revoluções, declarações de independência etc.</p>
				
