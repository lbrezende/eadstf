						<p>Thomas Hobbes (1588-1679): filósofo e teórico político inglês. Na clássica obra O Leviatã, justificou o estabelecimento de um Estado soberano em um pacto social para a imposição da ordem, que permitiria aos homens sair do estado de natureza, em que haveria constante guerra de todos contra todos, e viver em paz e segurança.</p>
						<p>John Locke (1632-1704): filósofo inglês tido como um dos principais teóricos do liberalismo e do contrato social. Além de ter sido um dos desenvolvedores das doutrinas do direito natural e da separação dos poderes, também se destacou pela defesa da tolerância religiosa.</p>
						<p>Jean-Jacques Rousseau (1712-1778): filósofo e teórico político suíço. Sua principal obra, Do Contrato Social, em que discute a legitimidade do Estado como derivada da vontade do povo, é considera uma das grandes influências políticas da Revolução Francesa.</p>
