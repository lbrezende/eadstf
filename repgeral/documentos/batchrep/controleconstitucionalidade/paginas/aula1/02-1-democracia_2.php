				<p>Do lado teórico, a partir do século XVIII, a ideia de que é a vontade do povo que constitui a comunidade política e determina os limites do poder político passou a ganhar espaço, pelo menos no mundo ocidental, em detrimento das formas de legitimação do poder político até então estabelecidas, tais como o poder divino e a tradição. Alguns dos mais importantes pensadores que contribuíram para isso foram <a href="https://goo.gl/jJNTs0" rel="external" target="_blank">Thomas Hobbes</a>, <a href="https://goo.gl/a2Vchz" rel="external" target="_blank">John Locke</a> e <a href="https://goo.gl/rizo5" rel="external" target="_blank">Jean-Jacques Rousseau</a>.</p>
			
