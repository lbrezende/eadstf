				<h3>4. Poder Constituinte</h3>
				
				<p>Poder Constituinte é o poder invocado para a promulgação de uma norma constitucional. Trata-se de um dos conceitos mais controversos da teoria política, e, por isso, não é nosso propósito nos estendermos muito sobre ele.</p>
				
				<p><strong>Poder Constituinte originário</strong> é o poder que funda a ordem constitucional. Podemos dizer que é um poder de fato, já que a ordem jurídica constitucional nasce a partir dele, e com ela todas as regras de distribuição de poder político. Em tese, é ilimitado.</p>
				
				<p>A atual Constituição da República Federativa do Brasil foi promulgada, em 1988, pela Assembleia Nacional Constituinte instalada entre 1987 e 1988, com a finalidade de, no exercício do poder constituinte originário, elaborar, no fim do regime militar, uma Constituição democrática para o país.</p>
				
				<p><strong>Poder Constituinte derivado</strong> é o poder de revisão ou alteração de normas da Constituição. Não é mais um poder ilimitado, porque está vinculado à forma prevista na própria Constituição.</p>
				
