				<p>O Brasil adota um sistema misto, no qual o Supremo Tribunal Federal reúne características tanto de tribunal constitucional quanto de corte suprema.</p>
				
				<p>Essa estrutura tem importância, como veremos, para a definição dos diferentes caminhos pelos quais uma questão constitucional chega a ser apreciada e decidida pelo Supremo Tribunal Federal — ora de modo direto (a que chamamos <?php _el('glossario','competência originária'); ?>), ora por meio de um recurso contra decisões de outros tribunais (a que chamamos <?php _el('glossario','competência recursal'); ?>).</p>
				
				<p>Além das competências estritamente relacionadas ao controle de constitucionalidade das leis, o Supremo Tribunal Federal acumula outras competências, também originárias, que não serão abordadas neste curso por não estarem diretamente relacionadas ao controle de constitucionalidade. A ação penal originária contra detentores de <strong>foro por prerrogativa de função</strong> é exemplo dessas outras competências. A competência originária que nos interessa neste curso é a que diz respeito a uma tarefa específica: o <strong>controle concentrado de constitucionalidade</strong>.</p>
			</div>
