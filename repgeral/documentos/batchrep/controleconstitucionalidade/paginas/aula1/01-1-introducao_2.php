				<p>No exercício do <strong>controle concentrado</strong> ou <strong>controle abstrato</strong> de constitucionalidade, o Supremo Tribunal Federal julga a constitucionalidade de uma lei ou ato normativo em tese ou, como o nome diz, em abstrato. Isso quer dizer que o que está em julgamento é a validade da própria lei, e não necessariamente a sua aplicação a um caso concreto. O texto da lei é comparado com o texto da Constituição e, caso ele contrarie algum dispositivo da Constituição, a lei é tida como inválida. Esse tipo de controle também é chamado de concentrado porque, ao invés de ser realizado simultaneamente por todos os órgãos do Poder Judiciário, é concentrado em um único órgão, o STF.</p>
				
				<p>A rigor, controle concentrado e controle abstrato de constitucionalidade não significam exatamente a mesma coisa. Contudo, existe uma sobreposição suficiente para permitir o uso intercambiável dessas expressões para se referir às seguintes modalidades de controle de constitucionalidade, que examinaremos mais detidamente no decorrer do curso:</p>
				
				<ol class="lista lista-alpha">
					<li>a ação direta de inconstitucionalidade (ADI);</li>
					<li>a ação direta de inconstitucionalidade por omissão (ADO);</li>
					<li>a ação declaratória de constitucionalidade (ADC); e</li>
					<li>a arguição de descumprimento de preceito fundamental (ADPF).</li>
				</ol>
				
