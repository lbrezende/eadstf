					<figcaption>Sede do Bundesverfassungsgericht, o Tribunal Constitucional Federal da Alemanha.</figcaption>
				</figure>
				
				<p><strong>Órgão de cúpula do Poder Judiciário</strong> ou <strong>Suprema Corte ou Supremo Tribunal</strong> refere-se, em geral, à última instância recursal, especialmente nos regimes em que todos os juízes e tribunais têm competência para decidir sobre a constitucionalidade das leis ao examinar sua aplicação nos casos que julgam. Nesse modelo, identificado como <strong>modelo norte-americano</strong>, o órgão de cúpula não é uma Corte especializada; é simplesmente aquele que tem a última palavra na decisão dos casos judiciais.</p>
				
