			<div class="col s12 m6">
				
				<p>Caro aluno, como servidor do Supremo Tribunal Federal (STF), sem dúvida você já tem ciência sobre o papel desse órgão como <strong>guardião da Constituição</strong>. Mas o que isso significa na prática? Significa que, como <strong>Corte Constitucional e órgão de cúpula do Poder Judiciário</strong>, o Supremo Tribunal Federal exerce o controle de constitucionalidade das <strong>leis</strong> e dos <strong>atos normativos</strong>.</p>
				<p>Embora compartilhem muitos pontos em comum, os conceitos de <strong>Corte Constitucional</strong> e de <strong>órgão de cúpula do Poder Judiciário</strong> são um pouco diferentes.</p>
				<p><strong>Corte Constitucional ou Tribunal Constitucional</strong> designa um órgão especializado cuja tarefa precípua é decidir sobre a constitucionalidade das leis. Uma Corte Constitucional pode integrar a estrutura ordinária do Poder Judiciário ou funcionar paralelamente a este. Esse modelo, bastante adotado na Europa, é chamado de <strong>modelo austríaco</strong>.</p>
			</div>
			<div class="col s12 m6">
				
				
				<figure class="fig fig-left fig-w40" aria-hidden="true">
