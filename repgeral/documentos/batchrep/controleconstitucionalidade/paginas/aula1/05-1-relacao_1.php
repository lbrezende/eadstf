				<h3>5. Relação entre Constituição e democracia</h3>
			</div>
			<div class="col s12 l6">
				
				<p>Como acabamos de ver, os dois pilares do Estado Democrático de Direito são o regime democrático (o poder político pertence ao povo) e a submissão a uma Constituição (a atividade do Estado é pautada, e os seus poderes limitados, por uma Constituição).</p>
				
				<p>Neste ponto, talvez você já tenha percebido que parece haver uma relação paradoxal entre constitucionalismo e democracia.</p>
				
				<p>Ao mesmo tempo em que o princípio democrático coloca o poder político nas mãos do povo (ou pelo menos dos seus representantes eleitos), o princípio constitucional limita esse poder. De um lado, ao impor limites ao exercício do poder, a Constituição impõe restrições à democracia. Nem todas as leis, ainda que atendam ao princípio majoritário, serão válidas se não forem compatíveis com a Constituição (se violarem direitos fundamentais, por exemplo).</p>
				
				<p>Na prática, a função da Constituição é justamente remover certas decisões do processo democrático. Como, então, podem ser conciliados a democracia e o constitucionalismo? O que pode justificar um sistema em que a vontade do Poder Constituinte se sobrepõe à vontade da maioria?</p>
			
			</div>
