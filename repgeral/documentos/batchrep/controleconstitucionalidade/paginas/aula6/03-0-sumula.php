				<h3>3. A Súmula Vinculante</h3>
			</div>
			<div class="col s12 l6">
				
				<p>Não podemos deixar de mencionar outra importante alteração trazida pela <strong>Emenda Constitucional 45/2004</strong>: a súmula vinculante, prevista no <strong>artigo 103-A da Constituição</strong>. Desde que o instituto foi criado, mais de cinquenta súmulas vinculantes já foram editadas. Vamos estudar uma delas para entender melhor como são criadas e qual é o seu papel no nosso sistema jurídico.</p>
				
				<p>De um modo geral, a finalidade das súmulas vinculantes é evitar a existência de decisões fundadas em interpretações dissonantes quando o Supremo Tribunal Federal já pacificou o seu entendimento sobre uma matéria. Isso torna as decisões judiciais mais previsíveis, aumentando a sensação de segurança jurídica na sociedade, além de ter impacto na qualidade e na velocidade da atuação judicial, visto que a vinculação dos juízes à orientação firmada pelo STF desestimula a interposição de recursos.</p>
				
			</div>
			<div class="col s12 l6">
				
				<p>Nossa primeira indagação é: em quais casos o Supremo Tribunal Federal pode editar súmula vinculante? As condições para a edição de súmula vinculante são previstas no <em>caput</em> e no § 1º do artigo 103-A da Constituição. Podemos sistematizá-las da seguinte maneira:</p>
				
				<ol class="lista-num">
					<li>a matéria objeto de súmula vinculante deverá ser constitucional;</li>
					<li>a súmula vinculante deverá contemplar a validade, a interpretação ou a eficácia de normas determinadas;</li>
					<li>deve haver decisões reiteradas do STF sobre a matéria;</li>
					<li>deve existir controvérsia atual entre órgãos judiciários ou entre o Poder Judiciário e a Administração pública;</li>
					<li>a controvérisa deve gerar insegurança jurídica;</li>
					<li>deve haver potencial de multiplicação de processos sobre questão idêntica.</li>
				</ol>
				
			</div>
