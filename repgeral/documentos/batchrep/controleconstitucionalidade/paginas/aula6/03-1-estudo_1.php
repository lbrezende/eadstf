				<h4>3.1 Estudo de caso — a Súmula vinculante 49</h4>
			</div>
			<div class="col s12 l7">
				
				<p>Para compreender melhor o que essas condições significam, vamos analisar a <strong>Súmula Vinculante 49</strong>, que diz o seguinte: “Ofende o princípio da livre concorrência lei municipal que impede a instalação de estabelecimentos comerciais do mesmo ramo em determinada área”.</p>
				
				<h6>1º) Qual é a matéria constitucional da Súmula Vinculante 49?</h6>
				
				<p>A questão jurídica tratada na súmula é a constitucionalidade de lei municipal que proíbe a instalação de estabelecimentos comerciais em área na qual já exista outro estabelecimento do mesmo ramo.</p>
				
				<p>Os Ministros do Supremo Tribunal Federal entenderam que, além do <strong>princípio da livre concorrência</strong>, consagrado no <strong>artigo 170, IV, da Constituição da República</strong> entre os princípios fundamentais da ordem econômica, a matéria objeto da Súmula Vinculante 49 também diz respeito ao comando inscrito no artigo 173, § 4º, da Constituição, segundo o qual “a lei reprimirá o abuso do poder econômico que vise à dominação dos mercados, à eliminação da concorrência e ao aumento arbitrário dos lucros”. O entendimento é que as leis de que trata a Súmula Vinculante 49 fazem exatamente o contrário do que determina o comando constitucional: promovem o abuso do poder econômico e a dominação dos mercados, eliminam a concorrência e propiciam aumento arbitrário de lucros.</p>
				
			</div>
			<div class="col s12 l5">
				
				<h6>2º) O STF já havia decidido sobre a matéria da Súmula Vinculante 49?</h6>
				
				<p>A aprovação da Súmula Vinculante 49 teve amparo na existência de diversas decisões do Supremo Tribunal Federal quanto à inconstitucionalidade de leis municipais com o mesmo conteúdo. Serviram de referência para a aprovação da súmula as seguintes decisões do Tribunal nos seguintes processos: <strong>RE 438.485</strong>, <em>DJe</em> de 5-5-2011; <strong>AI 764.788</strong>, <em>DJe</em> 203 de 28-10-2009; <strong>AC 1.440</strong>, <em>DJ</em> de 13-11-2006; <strong>RE 193.749</strong>, <em>DJ</em> de 4-5-2001; <strong>RE 202.832</strong>, <em>DJ</em> de 22-10-1999; <strong>RE 198.107</strong>, <em>DJ</em> de 6-8-1999; <strong>RE 200.572</strong>, <em>DJ</em> de 17-5-1999; <strong>RE 207.506</strong>, <em>DJ</em> de 10-5-1999; <strong>AI 239.299</strong>, <em>DJ</em> de 25-5-1999; <strong>RE 199.517</strong>, <em>DJ</em> de 13-11-1998; <strong>RE 217.029</strong>, <em>DJ</em> de 28-9-1998; <strong>RE 203.909</strong>, <em>DJ</em> de 6-2-1998.</p>
				
				<p>Já existiam, portanto, decisões reiteradas apoiando a edição de uma súmula vinculante sobre essa matéria.</p>
				
