				<h4>3.3. Efeitos</h4>
				
				<p>O nome não dá margem a dúvidas: a súmula vinculante vincula a atuação dos demais órgãos do Poder Judiciário e da Administração Pública Federal, estadual e municipal. Isso quer dizer que, ao se depararem com questão constitucional sobre a qual o Supremo Tribunal Federal já editou súmula vinculante, juízes e tribunais devem seguir o entendimento ali consolidado.</p>
				
				<p>Além disso, os Tribunais podem recusar recursos ao constatar que a decisão contra a qual eles foram interpostos está em harmonia com uma súmula vinculante.</p>
				
