				<h3>1. Introdução</h3>
			</div>
			<div class="col s12 l6">
				
				<p>Bem-vindo! Após duas aulas estudando o controle <strong>concentrado</strong> de constitucionalidade, vamos abordar, nesta aula, o controle de constitucionalidade <strong>difuso</strong>! Lembra-se da diferença, certo?</p>
				
				<p>Se não, antes de prosseguir, você pode refrescar a memória relendo a primeira parte da <strong>Aula 4</strong>.</p>
				
				<p>A esta altura do curso, você já sabe que o controle difuso de constitucionalidade pode ser exercido por todos os juízes do Brasil, em qualquer caso sob sua responsabilidade, desde que uma questão constitucional se faça presente.</p>
				
				<p>Você também aprendeu que o controle difuso tem lugar nos <strong>processos subjetivos</strong>, cuja finalidade primeira não é obter uma declaração sobre a constitucionalidade da lei, e, sim, decidir sobre os direitos e interesses das partes em litígio. A questão constitucional, quando aparece, é secundária, lateral, incidental, uma questão que precisa ser resolvida para que a solução daquele caso seja dada, mas não a questão sobre a qual estão as partes disputando.</p>
				
			</div>
			<div class="col s12 l6">
				
				<p>Viu também que a declaração de inconstitucionalidade, no controle difuso, vale somente para a disputa travada no processo em que ela foi suscitada. O juiz tem que se manifestar sobre a questão constitucional para decidir o caso, mas essa decisão não vincula outros juízes, outros casos, nem outras partes.</p>
				
				<p>Quem pode requerer um pronunciamento sobre a constitucionalidade de determinada norma em um processo subjetivo, tal como uma ação penal ou uma reclamação trabalhista? A parte interessada naquela declaração. Pode ser o autor da ação, se o fundamento do seu pedido tiver como base a invalidade de alguma lei perante a Constituição.</p>
				
				<p>O réu, por sua vez, poderá ter interesse em requerer a declaração de inconstitucionalidade da lei que ampara o pedido do autor contra ele.</p>
				
				<p>Terceiros que tenham sido admitidos a participar do processo por terem interesse no seu desfecho também podem suscitar a declaração de inconstitucionalidade.</p>
				
