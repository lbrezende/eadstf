			<div class="col s12 l6">
				
				<p>Terceiros que tenham sido admitidos a participar do processo por terem interesse no seu desfecho também podem suscitar a declaração de inconstitucionalidade.</p>
				
				<p>O Ministério Público, quando intervém no processo, mesmo não sendo parte, pode fazê-lo se achar pertinente.</p>
				
				<p>E ainda que ninguém tenha solicitado, o próprio juiz, se julgar que a solução correta do caso depende de prévia decisão sobre a questão constitucional, pode vir a declarar a inconstitucionalidade de uma norma que, do contrário, seria aplicável.</p>
				
				<p>Em regra, das decisões judiciais de primeiro grau, inclusive quando enfrentam questões constitucionais, cabe recurso da parte inconformada para o tribunal ao qual está vinculado o juiz que a proferiu (das decisões do Juiz Eleitoral, para o Tribunal Regional Eleitoral; das decisões do Juiz do Trabalho, para o Tribunal Regional do Trabalho; das decisões do Juiz de Direito, para o Tribunal de Justiça; das decisões do Juiz Federal, para o Tribunal Regional Federal).</p>
				
			</div>
			<div class="col s12 l6">
				
				<p>Persistindo, após os recursos cabíveis, insatisfação quanto à solução da questão constitucional e não mais cabendo outro recurso, poderá ser provocado o Supremo Tribunal Federal a se manifestar sobre ela por meio do <strong>recurso extraordinário</strong>.</p>
				
				<p>Ele será o foco do nosso estudo sobre o controle difuso de constitucionalidade exercido no Supremo Tribunal Federal.</p>
				
				<p>Atenção: O controle difuso de constitucionalidade não se resume ao recurso extraordinário. Ele pode ocorrer a qualquer momento do processo, em qualquer tribunal. O recurso extraordinário é uma etapa do controle difuso, que permite a sua revisão pelo Supremo Tribunal Federal.</p>
				
