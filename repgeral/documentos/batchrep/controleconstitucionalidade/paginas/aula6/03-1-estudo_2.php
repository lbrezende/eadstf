				<h6>3º) A Súmula Vinculante 49 contempla a validade, a interpretação ou a eficácia de normas determinadas?</h6>
				
				<p>A Súmula Vinculante 49 tem como objeto a <strong>validade</strong>, à luz da Constituição, de normas com um determinado conteúdo: leis municipais proibindo a instalação de estabelecimentos comerciais em área na qual já exista outro estabelecimento do mesmo ramo. Segundo essa Súmula, a posição do STF é de que essas normas são <strong>inválidas</strong>.</p>
				
				<h6>4º, 5º e 6º) A edição da súmula encontrou justificativa na existência de controvérsia atual entre órgãos judiciários ou entre o Poder Judiciário e a Administração pública? Essa controvérsia acarretava insegurança jurídica? Essa controvérsia acarretava a multiplicação de processos sobre questão idêntica?</h6>
				
				<p>O Tribunal respondeu sim para as três perguntas. No julgamento da proposta de súmula vinculante que levou à edição da Súmula Vinculante 49, ficou assentado que, apesar dos reiterados precedentes, o STF frequentemente era chamado a novamente se pronunciar sobre a matéria. Isso evidenciaria que leis com esse tipo de conteúdo continuavam a ser regularmente deliberadas nas câmaras municipais Brasil afora, gerando insegurança jurídica. Foi constatado também que o tema tinha um efeito multiplicador, podendo se repetir em inúmeros processos que discutiam questão idêntica.</p>
				
