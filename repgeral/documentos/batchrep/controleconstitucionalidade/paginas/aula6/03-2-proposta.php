				<h4>3.2 A Proposta de Súmula Vinculante (PSV)</h4>
			</div>
			<div class="col s12 l5">
				
				<p>O primeiro passo para se aprovar uma súmula vinculante é a apresentação de Proposta de Súmula Vinculante (PSV) perante o STF. A <strong>Súmula Vinculante 49</strong> foi aprovada pelo Plenário do STF em junho de 2015 e foi o resultado de processo iniciado em setembro de 2013, com a apresentação da <strong>Proposta de Súmula Vinculante 90</strong>.</p>
				
				<p>Formalizada a proposta de súmula vinculante, o STF a ela dará publicidade, mediante edital disponibilizado durante vinte dias no sítio eletrônico do Tribunal e no Diário da Justiça eletrônico, a fim de que os interessados possam dela tomar ciência. Após esse período, os interessados terão o prazo de cinco dias para manifestarem-se por escrito sobre a proposta.</p>
				
				<p>Em seguida, colhe-se a manifestação dos Ministros integrantes da Comissão de Jurisprudência do STF, em relação aos aspectos formais da proposta. O passo seguinte é o envio de cópias do processo aos demais Ministros e ao Procurador-Geral da República. Por fim, o Presidente do STF submete a proposta à deliberação do Plenário.</p>
				
				<p>O quórum para a aprovação de súmula vinculante é de dois terços dos membros do STF, ou seja, oito Ministros. A Súmula Vinculante 49 foi aprovada na sessão do Plenário realizada em 17-6-2015, por unanimidade.</p>
				
			</div>
			<div class="col s12 l7">
				
				<div class="box box-destaque2">
					
					<h6>Quem pode requerer a edição de uma súmula vinculante?</h6>
					
					<p>Segundo a Constituição, além de ser permitido ao STF aprovar súmula vinculante por iniciativa própria, os legitimados para propor a ação direta de inconstitucionalidade (já estudamos quem são!) podem requerer a edição de súmula vinculante. A Constituição autoriza, ainda, que a lei estenda a outros entes ou autoridades a legitimidade para propor a edição de súmula vinculante ao STF. Com isso, foram incluídos o Defensor Público-Geral da União, os Tribunais Superiores, os Tribunais de Justiça, os Tribunais Regionais Federais, do Trabalho e Eleitorais, os Tribunais Militares e, em alguns casos, os Municípios.</p>
					
