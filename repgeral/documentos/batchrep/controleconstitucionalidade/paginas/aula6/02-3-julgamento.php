				<p>Uma vez reconhecida a repercussão geral de uma questão, todos os demais processos nos quais a mesma questão seja discutida ficam sobrestados. Em outras palavras, ficam congelados, aguardando a decisão final do STF sobre o mérito daquela questão.</p>
				
			</div>
			<div class="col s12 l6">
				
				<p>Com a repercussão geral, o julgamento do STF, no recurso extraordinário, assume ares semelhantes aos do controle concentrado. Isso porque, uma vez que o STF reconheça a repercussão geral de uma matéria, a sua decisão valerá para todos os demais casos em que aquela mesma discussão se repetir.</p>
				
				<p>O STF julga um caso concreto, mas o entendimento que levou o STF a chegar àquela decisão será enunciado por meio de uma <strong>tese</strong> e vinculará os casos idênticos.</p>
				
