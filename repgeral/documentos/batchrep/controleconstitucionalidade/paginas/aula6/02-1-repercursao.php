				<h4>2.1 A repercussão geral</h4>
			</div>
			<div class="col s12 l6">
				
				<p>O instituto da repercussão geral do recurso extraordinário é relativamente recente. Foi introduzido pela Emenda Constitucional 45/2004, que acrescentou o § 3º ao artigo 102 da Constituição. Diz ele:</p>
				
				<div class="box box-citacao">
					<blockquote>
						<p>No recurso extraordinário o recorrente deverá demonstrar a repercussão geral das questões constitucionais discutidas no caso, nos termos da lei, a fim de que o Tribunal examine a admissão do recurso, somente podendo recusá-lo pela manifestação de dois terços de seus membros.</p>
					</blockquote>
				</div>
				
				<p>Se a parte quiser que o Supremo Tribunal Federal julgue o seu recurso, precisa convencer pelo menos um terço dos Ministros de que o seu processo não apenas envolve uma questão constitucional, mas que essa questão tem <strong>repercussão geral</strong>. Neste momento, imagino que você esteja se fazendo a seguinte pergunta: o que é que significa possuir repercussão geral?</p>
				
				<p>A resposta é dada pelo <strong>artigo 1.035, § 1º, do Código de Processo Civil</strong>: “Para efeito de repercussão geral, será considerada a existência ou não de questões relevantes do ponto de vista econômico, político, social ou jurídico que ultrapassem os interesses subjetivos do processo”.</p>
				
			</div>
			<div class="col s12 l6">
				
				<p>Isso quer dizer que a controvérsia relativa à interpretação ou aplicação da Constituição ocorrida naquele processo é suficientemente relevante para interessar não apenas às partes em litígio. Trata-se, na prática, de um juízo de relevância, um meio de o STF filtrar os recursos a ele encaminhados não apenas por aspectos estritamente processuais, mas também por um aspecto valorativo. Se dois terços dos Ministros entenderem que a questão não tem relevância econômica, política, social ou jurídica, ela não será apreciada pelo STF, valendo, naquele caso, a decisão tomada pelo Tribunal de origem.</p>
				
				<p>A inclusão da repercussão geral entre os requisitos de admissibilidade do recurso extraordinário foi uma resposta a um problema prático: a enorme quantidade de recursos extraordinários que chegava ao Supremo Tribunal Federal obrigava a Corte a dedicar muito tempo e energia para tratar de questões pouco relevantes, desviando a sua atenção das grandes questões sociais, econômicas, políticas e jurídicas do país.</p>
				
				<p>A repercussão geral alterou profundamente o processamento do recurso extraordinário no Supremo Tribunal Federal. No atual regime, podemos identificar duas fases principais. Na primeira, o STF se manifesta sobre a existência ou não de repercussão geral. Na segunda, o STF julga o mérito do recurso extraordinário.</p>
				
