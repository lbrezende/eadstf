				<h4>2.2 Manifestação sobre a repercussão geral</h4>
			</div>
			<div class="col s12 l6">
				
				<p>Neste primeiro momento, o Tribunal não decide se o recorrente tem ou não razão. Decide apenas se o recurso extraordinário atende ou não ao requisito da repercussão geral. Em outras palavras, decide se a questão constitucional discutida naquele recurso extraordinário é suficientemente relevante politicamente, juridicamente, economicamente ou socialmente para justificar a atuação do Supremo Tribunal Federal.</p>
				
				<p>A apreciação sobre a presença ou ausência da repercussão geral é feita no ambiente eletrônico chamado <strong>Plenário Virtual</strong>, ao qual têm acesso os Ministros do STF. Funciona assim: o relator do recurso extraordinário submete ao Plenário Virtual a sua manifestação quanto à presença ou ausência da repercussão geral relativamente ao tema discutido naquele recurso.</p>
				
				<figure class="fig lightboxed">
					<img src="img/aula6/print-plenario-virtual.png" alt="" role="presentation" style="max-width: 1440px;">
					<figcaption>Plenário Virtual</figcaption>
				</figure>
				
			</div>
			<div class="col s12 l6">
				
				<p> O tema fica disponível para a manifestação dos demais Ministros por um período de vinte dias.</p>
				
				<p>Para ser rejeitada a existência da repercussão geral, é necessário o voto de dois terços dos Ministros do STF (oito votos).</p>
				
				<p>A rejeição da repercussão geral não significa necessariamente que o STF endosse a solução a que chegou a decisão recorrida, mas apenas que o STF não julgará a questão, prevalecendo a decisão dada na origem.</p>
				
				<p>Admitida a repercussão geral, para o que é suficiente o voto favorável ou a ausência de oposição (silêncio) de apenas quatro Ministros, o recurso extraordinário terá o seu mérito julgado pelo Plenário do STF, em sessão presencial.</p>
				
