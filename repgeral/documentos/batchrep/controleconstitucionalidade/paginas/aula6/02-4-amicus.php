				<h4>2.4 <em>Amicus curiae</em></h4>
				
				<p>Tendo em vista que o regime da repercussão geral confere ao julgamento do recurso extraordinário características de processo objetivo, uma vez que valerá para todos os casos envolvendo a mesma hipótese — com a elaboração de uma tese geral —, embora a decisão do Supremo Tribunal Federal seja tomada sobre um caso concreto, passou-se a admitir a participação de <em>amicus curiae</em> neste tipo de processo.</p>
				
				<p>Se é pressuposto para que o Supremo Tribunal Federal julgue o recurso extraordinário, no regime da repercussão geral, o fato de a relevância da questão constitucional nele veiculada ultrapassar os interesses pessoais das partes no processo, nada mais adequado do que permitir que os setores da sociedade interessados na decisão, ou por ela afetados, tenham direito de contribuir com a Corte com novos elementos, informações e pontos de vista, visando a aperfeiçoar a compreensão do tema.</p>
				
