				<h3>2. O recurso extraordinário e a repercussão geral</h3>
			</div>
			<div class="col s12 l6">
				
				<p>Previsto no <strong>artigo 102, III, da Constituição</strong>, o recurso extraordinário, como o próprio nome indica, é (ou deveria ser) extraordinário, excepcional. Espera-se que a maioria dos casos judiciais sejam resolvidos pelos diversos juízes e tribunais e apenas um reduzido número de casos precise chegar ao STF. Na prática, especialistas constatam abuso no uso do recurso extraordinário, que tem como efeito abarrotar o Supremo Tribunal de recursos sem expectativa alguma de sucesso.</p>
				
				<p>A ideia por trás do recurso extraordinário é a de que, por mais que todos os juízes e tribunais tenham competência para, no julgamento dos casos a eles submetidos, interpretar a Constituição e, com base nisso, afastar a aplicação de lei tida como inconstitucional, é do Supremo Tribunal Federal a última palavra sobre as questões constitucionais. O mecanismo pelo qual o Supremo Tribunal Federal revisa o entendimento dos demais juízes e tribunais sobre a Constituição no julgamento de determinado caso é o recurso extraordinário.</p>
				
			</div>
			<div class="col s12 l6">
				
				<p>Por não ser um recurso comum, ordinário, o recurso extraordinário precisa cumprir diversos requisitos formais para que o Supremo Tribunal Federal aceite examiná-lo.</p>
				
				<p>De modo geral, esses requisitos funcionam como filtros para barrar, por exemplo, recursos discutindo questões que não dizem respeito à aplicação ou à interpretação da Constituição, ou questões que o STF já decidiu anteriormente, ou recursos pedindo ao STF que se pronuncie sobre questão constitucional que sequer foi abordada naquele caso. A maioria desses requisitos está fora do escopo deste curso, mas há um deles sobre o qual estudaremos um pouco mais adiante: o instituto da <strong>repercussão geral</strong>.</p>
				<div class="box box-destaque2">
					
					<p><strong>Quem pode apresentar o recurso extraordinário?</strong></p>
					
					<p>O recurso extraordinário pode ser apresentado por qualquer uma das partes envolvidas em uma disputa judicial, seja o autor, o réu, um terceiro interessado admitido no processo ou o Ministério Público.</p>
					
