				<p>Relativamente aos índios, no entanto, a questão constitucional mais conflituosa diz respeito às terras por eles ocupadas. Não são raros os conflitos fundiários entre índios e não índios (posseiros, agricultores, pecuaristas, madeireiros, garimpeiros etc.) Brasil adentro.</p>
				
				<p>A Constituição da República (<strong>artigo 231</strong>) traz  um regime especial de tutela dos direitos indígenas, assegurando-lhes, entre outros, “os direitos originários sobre as terras que tradicionalmente ocupam”. Compete à União demarcar e proteger esse direito, o que nem sempre é cumprido.</p>
				
