				<h3>5. Quilombolas</h3>
			</div>
			<div class="col s12 l6">
				
				<div class="row">
					<div class="col s12 m8">
						<p>Outro grupo étnico-cultural que também recebeu proteção especial da Constituição é o dos remanescentes das comunidades quilombolas.</p>
						
						<p>Quilombos, todos nós aprendemos nas aulas de história, são as comunidades formadas por escravos fugitivos, geralmente em regiões isoladas ou de difícil acesso, durante o triste período em que prosperou o regime escravocrata no Brasil. Muitas dessas comunidades persistiram até os dias de hoje e desenvolveram culturas e tradições próprias. Reconhecendo essa identidade, o artigo 68 do Ato das Disposições Constitucionais Transitórias assegura aos remanescentes das comunidades dos quilombos a propriedade das terras por eles ocupadas.</p>
						
					</div>
					<div class="col s12 m4">
						
						<div class="box box-destaque">
							<p><strong>Art. 68.</strong> Aos remanescentes das comunidades dos quilombos que estejam ocupando suas terras é reconhecida a propriedade definitiva, devendo o Estado emitir-lhes os títulos respectivos.</p>
						</div>
						
					</div>
				</div>
				
			</div>
			<div class="col s12 l6">
				
				<p>A extensão dos direitos territoriais assegurados pela Constituição aos remanescentes dos quilombos ainda é objeto de intensa controvérsia, bem como os critérios para a sua identificação, reconhecimento e demarcação das terras. Está em discussão no Supremo Tribunal Federal, ainda sem conclusão, o julgamento da <a href="https://goo.gl/mk8V1u" class="ui-link" rel="external" target="_blank">ADI 3.239</a>, em que se espera serão esclarecidos vários desses aspectos.</p>
				
