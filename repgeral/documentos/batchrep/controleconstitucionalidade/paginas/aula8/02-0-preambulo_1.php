				<p>Ao mencionar Deus, embora sem referência a uma religião específica, a Constituição espelha o fato de que a crença religiosa é importante para a maioria dos brasileiros. Ao mesmo tempo, transparece a diversidade da expressão do sentimento religioso na população, que não se apresenta de modo uniforme em um país formado por parcelas significativas de católicos, evangélicos, espíritas, seguidores de religiões de matriz africana, pessoas sem religião, entre outros.</p>
				
