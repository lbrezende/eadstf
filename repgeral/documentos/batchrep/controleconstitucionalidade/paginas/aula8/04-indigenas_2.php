			<div class="col s12 l6">
				
				<p>No julgamento da <strong>Pet 3.388/RR</strong>, o Supremo Tribunal Federal, ao apreciar conflito envolvendo a demarcação da Terra Indígena Raposa Serra do Sol, localizada no estado de Roraima, teve a oportunidade de fixar importantes balizas interpretativas dos direitos assegurados aos índios na Constituição brasileira, dentre os quais, vale destacar:</p>
				
				<ul class="setas2">
					
					<li>índios em processo de aculturação, e não apenas aqueles em estágio primitivo, fazem jus à proteção constitucional;</li>
					
					<li>as terras indígenas são bens públicos federais e os direitos originários dos índios sobre elas antecede a formação dos territórios dos estados federados, que lhes devem respeito. Por outro lado, a demarcação de terras indígenas não as subtrai dos territórios dos estados;</li>
					
					<li>o procedimento de demarcação das terras indígenas compete somente à União;</li>
					
					<li>os dispositivos da Constituição que consagram a proteção dos direitos indígenas visam a compensar desvantagens historicamente acumuladas e devem ser interpretados à luz do princípio da fraternidade para com as minorias;</li>
					
				</ul>
				
			</div>
			<div class="col s12 l6">
				
				<ul class="setas2">
					
					<li>o desenvolvimento nacional não pode ser feito às custas do equilíbrio ambiental, tampouco sem incorporar a realidade indígena;</li>
					
					<li>a proteção da Constituição incide sobre as terras ocupadas por índios na data de sua promulgação. A exceção são os casos em que, embora interrompida ou perturbada a ocupação indígena em razão de hostilidades, mantiveram eles, sem ter se conformado, a resistência e a pretensão sobre as terras;</li>
					
					<li>a demarcação de terras indígenas não é incompatível com a instalação de equipamentos públicos, a abertura de estradas e outras vias de comunicação e a eventual presença de não índios;</li>
					
					<li>é possível sobrepor a demarcação de terras indígenas a áreas de conservação ambiental, por serem compatíveis.</li>
					
				</ul>
				
