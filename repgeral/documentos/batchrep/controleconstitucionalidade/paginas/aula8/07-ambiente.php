				<p>Certas características físicas do Brasil fazem com que a proteção do meio ambiente assuma, no país, uma importância crucial. País de dimensões continentais, o Brasil conta com 7.367 km de costa e abriga, entre outras paisagens naturais, a maior parte da Floresta Amazônica, a maior floresta tropical remanescente no planeta e um dos ecossistemas com maior biodiversidade em todo o mundo. O fato de que os efeitos da degradação desses sistemas têm potencial para repercutir no equilíbrio do planeta considerado como um todo atribui ao Brasil uma responsabilidade moral perante a comunidade internacional, no tocante à sua preservação para as futuras gerações.</p>
				
				<p>A conscientização em relação à necessidade de preservar o meio ambiente é um fenômeno recente, que tomou corpo apenas na segunda metade do século XX. Em razão disso, o tema praticamente não aparece em constituições com mais de cinquenta anos. Mesmo os textos mais recentes, em grande parte, limitam-se a enunciar a proteção do meio ambiente como um princípio a ser observado. O tratamento abrangente recebido pela matéria na Constituição do Brasil o coloca em um lugar de destaque no tocante à constitucionalização do direito ao meio ambiente ecologicamente equilibrado.</p>
				
			</div>
			<div class="col s12 l5">
				
				<p>Nesse sentido, a Constituição brasileira proclama o direito de todos, presentes e futuras gerações, ao meio ambiente ecologicamente equilibrado (<?php _el('legis','art. 225, <em>caput</em>');?>). Isso quer dizer que a Constituição condiciona a exploração legítima dos recursos naturais ambientais por uma geração ao limite em que as gerações seguintes deles não sejam privadas.</p>
				
				<p>Uma norma estipulando cotas de pesca superiores à capacidade de reprodução dos peixes em uma determinada região, de tal modo que os recursos pesqueiros se esgotariam ou ficariam significativamente reduzidos em poucas décadas, estaria em choque com o direito das futuras gerações de ter acesso e usufruir desses recursos.</p>
				
				<p>Além disso, a Constituição (<?php _el('legis','art. 225, § 4°'); ?>) eleva determinadas áreas, como a Mata Atlântica remanescente, a Floresta Amazônica e o Pantanal, em razão de sua relevância para a preservação do meio ambiente e da biodiversidade, bem como sua vulnerabilidade, ao status de patrimônio nacional, de modo a permitir maior controle do seu uso.</p>
				
