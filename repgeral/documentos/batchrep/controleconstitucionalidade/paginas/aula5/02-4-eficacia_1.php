				<h4>2.4 Eficácia das normas constitucionais</h4>
			</div>
			<div class="col s12 l7">
				
				<p>A maioria dos direitos previstos diretamente na Constituição pode ser exercida independentemente da existência de lei sobre eles. Chamamos as normas constitucionais que preveem esses direitos de <strong>autoaplicáveis</strong>.</p>
				
				<p>Entre as normas autoaplicáveis há aquelas — como a que assegura a livre manifestação do pensamento, por exemplo — que têm o que chamamos de <strong>eficácia plena</strong>. São as normas constitucionais que estabelecem limites para a atuação do legislador e afirmam uma esfera de direitos que a lei não pode invadir. Em vez de impor ao legislador um dever de legislar, elas impõem um dever de não legislar. São vedações a que o legislador restrinja direitos assegurados na própria Constituição.</p>
				
				<p>Outro tipo de normas <strong>autoaplicáveis</strong> são as normas de <strong>eficácia contida</strong>. No caso do direito de propriedade, por exemplo, a lei pode estabelecer tributos sobre a transmissão da propriedade, regras de zoneamento urbano, regras ambientais para o uso da propriedade, formas de perda da propriedade (usucapião), mas o direito de propriedade em si já está garantido na Constituição. Promulgada a Constituição, esses direitos estão protegidos, o que não impede que lei venha a esclarecer o conteúdo desses direitos. Além disso, a lei não pode limitar esses direitos ao ponto de descaracterizá-los, sob pena de inconstitucionalidade.</p>
				
			</div>
			<div class="col s12 l5">
				
				<p>Nesses casos, a lei vem para delinear certos aspectos do direito, implementar algumas restrições — quando a Constituição o permitir — e organizar a sua proteção. O Poder Legislativo pode até vir a editar uma lei sobre essas matérias, mas não é constitucionalmente obrigado a fazê-lo e, se não o fizer, isso não prejudica a efetividade da norma. </p>
				
				<p>O Supremo Tribunal Federal já decidiu que normas autoaplicáveis não podem — justamente porque não precisam — ser tuteladas por ação direta de inconstitucionalidade por omissão. Vejamos:</p>
				
				<blockquote class="legislacao">
					<p>Ação direta de inconstitucionalidade por omissão, de que não se conhece, por ser autoaplicável o dispositivo constitucional (art. 20 do ADCT), cuja possibilidade de cumprimento pretende o Requerente ver suprida.</p>
					<p class="citacao">(<strong>ADI 297/DF</strong>, Relator Ministro Octavio Gallotti, Tribunal Pleno, julgamento em 25-4-1996, <em>DJe</em> 8-11-1996)</p>
				</blockquote>
				
