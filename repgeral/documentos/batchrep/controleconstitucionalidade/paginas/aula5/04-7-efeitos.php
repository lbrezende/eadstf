				<h4>4.7 Efeitos da decisão</h4>
				
				<p>Como as demais ações que integram o conjunto dos mecanismos de controle concentrado de constitucionalidade, a decisão de procedência (o autor tem razão) ou improcedência (o autor não tem razão) na arguição de descumprimento de preceito fundamental também tem eficácia contra todos e efeito vinculante.</p>
				
				<p>Como a ADPF alcança uma ampla gama de atos do Poder Público, a decisão pode assumir feições variadas, como, por exemplo, a declaração de inconstitucionalidade de um ato do Poder Público, seja ou não um ato normativo, a cassação de decisão judicial ou administrativa, a fixação do modo de interpretação do preceito fundamental ou a enunciação de norma para preencher uma lacuna no <strong>ordenamento jurídico</strong>.</p>
				
