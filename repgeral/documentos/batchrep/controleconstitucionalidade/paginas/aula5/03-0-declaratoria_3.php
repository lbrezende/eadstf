				<p class="saudacao">Vamos a um exemplo?</p>
				
				<p>Em 9-2-2012, o Supremo Tribunal Federal julgou a <strong>ADC 19</strong>, proposta pela Presidente da República com o objetivo de ver declarada a constitucionalidade de alguns artigos da <strong>Lei 11.340/2006</strong>, que você provavelmente conhece pelo seu apelido, <strong>Lei Maria da Penha</strong>. Essa lei prevê mecanismos específicos destinados à prevenção e à repressão da violência doméstica praticada contra a mulher. Na prática, trata com mais rigor a violência doméstica quando a vítima é mulher.</p>
				
				<p>Ocorre que alguns juízes estavam deixando de aplicar as regras da Lei Maria da Penha para lidar com casos de violência contra a mulher, por considerarem que ela seria incompatível com o <strong>artigo 5º, I, da Constituição da República</strong>, segundo o qual “homens e mulheres são iguais em direitos e obrigações”.</p>
				
				<p>Por <strong>unanimidade</strong>, o Plenário do STF julgou a ADC 19 procedente para declarar a <strong>constitucionalidade</strong> dos artigos da Lei Maria da Penha questionados. Prevaleceu o entendimento de que a desigualdade de tratamento criada pela lei, no caso, tem o efeito de neutralizar o real desequilíbrio verificado entre homens e mulheres na nossa sociedade de um modo geral. Nesse caso, o estabelecimento de uma desigualdade jurídica para compensar uma desvantagem social e cultural é um meio de se atingir a igualdade de fato e, por isso, está de pleno acordo com o princípio da igualdade.</p>
				
				<p>Como a decisão do Supremo Tribunal Federal na ação declaratória de constitucionalidade tem eficácia contra todos e efeito vinculante, juízes não podem mais se negar a aplicar a Lei Maria da Penha sob o pretexto de que ela seria inconstitucional.</p>
				
