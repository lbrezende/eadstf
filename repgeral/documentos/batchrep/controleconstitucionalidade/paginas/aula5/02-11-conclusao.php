				<h4>2.11 Conclusão</h4>
				
				<p>Os números mostram que a ação direta de inconstitucionalidade por omissão é bem menos utilizada que a ação direta de inconstitucionalidade, o que era esperado pelo seu próprio objeto. Existe número limitado de normas da Constituição estabelecendo deveres específicos de legislar. Por outro lado, são ilimitadas as possíveis leis inconstitucionais que podem ser criadas.</p>
				
				<p>Embora com espaço de atuação bem delimitado, a importância da ação direta de inconstitucionalidade por omissão, no universo dos instrumentos de controle concentrado de constitucionalidade, não pode ser desprezada. É um relevante meio para pressionar o Poder Legislativo e a Administração Pública a tomarem as medidas necessárias para proteger direitos fundamentais, em particular os <strong>direitos positivos</strong>.</p>
				
