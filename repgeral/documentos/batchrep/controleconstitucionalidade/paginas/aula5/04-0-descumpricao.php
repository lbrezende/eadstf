				<h3>4. A arguição de descumprimento de preceito fundamental</h3>
				
				<p>Encerraremos a parte do curso dedicada ao estudo dos instrumentos de controle concentrado de constitucionalidade com um olhar sobre uma ação que tem tido cada vez mais espaço. Inicialmente pouco acionada, a <strong>arguição de descumprimento de preceito fundamental (ADPF)</strong> ganhou importância nos últimos anos, o que se reflete no aumento que tem sido verificado no número de ações desse tipo ajuizadas.</p>
				
				<p>Entre as ações de controle concentrado de constitucionalidade, a arguição de descumprimento de preceito fundamental é a que gera mais controvérsias e, por isso, há muito a seu respeito que ainda não é consensual. Com a sua popularidade cada vez maior, espera-se que as dúvidas sobre a arguição de descumprimento de preceito fundamental sejam resolvidas em breve. Neste curso, vamos abordar alguns aspectos essenciais da ADPF, evitando adentrar nos seus pontos polêmicos, o que estaria fora do escopo de um curso introdutório.</p>
				
				<p>A arguição de descumprimento de preceito fundamental está prevista no art. 102, § 1º, da Constituição Federal, e o seu processo e julgamento são regulados pela Lei 9.882/1999. Vejamos alguns dos seus pontos principais.</p>
				
