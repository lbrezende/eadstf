				<h3>1. Introdução</h3>
				
				<p>Olá! Vamos dar início a mais uma aula do nosso curso <strong>Introdução ao direito constitucional e ao controle de constitucionalidade</strong>!</p>
				
				<p>Embora seja o mais utilizado mecanismo de controle abstrato, a ação direta de inconstitucionalidade, sobre a qual nos debruçamos na aula anterior, não alcança todas as possibilidades de lesão à Constituição. Por isso, outros instrumentos são previstos visando à proteção integral e efetiva das normas constitucionais. São eles: a ação direta de inconstitucionalidade por omissão (ADO); a ação declaratória de constitucionalidade (ADC); e a arguição de descumprimento de preceito fundamental (ADPF). O processo e o julgamento das duas primeiras são tratados na <strong>Lei 9.868/1999</strong>, a mesma lei que cuida da ação direta de inconstitucionalidade. A ADPF é regida pela <strong>Lei 9.882/1999</strong>.</p>
				
				<p class="saudacao center">Preparados para conhecer esses instrumentos? Então vamos lá!</p>
				
				
