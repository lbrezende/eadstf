				<p>Não é apenas ao editar lei incompatível com a Constituição que um órgão legislativo, como o Congresso Nacional, age de modo inconstitucional. Ele pode contrariar a Constituição simplesmente ao deixar de cumprir um comando constitucional para elaborar determinada lei. A Constituição contém diversas normas que configuram ordens destinadas ao Poder Legislativo, para que ele elabore a lei necessária para regular certa matéria ou proteger determinado direito. O descumprimento desse comando constitucional pelo legislador caracteriza uma inconstitucionalidade em si mesma, ainda que por <strong>omissão</strong>.</p>
			
