				<h4>2.6 Como se dá o processamento da ação direta de inconstitucionalidade por omissão?</h4>
				
				<p>Assim como ocorre com a ação direta de inconstitucionalidade, proposta a ação direta de inconstitucionalidade por omissão, ela é distribuída por sorteio a um dos Ministros do Supremo Tribunal Federal, que será o seu Relator — o Presidente é excluído da distribuição.</p>
				
				<p>O Relator será o responsável por cuidar do andamento do processo até que ele esteja pronto para julgamento e, como o nome diz, prepara o relatório para dar conhecimento do processo aos demais Ministros. Avalia se há pedido de medida cautelar e a urgência na sua concessão, o que vai determinar os próximos passos e os prazos a serem concedidos para as manifestações das autoridades envolvidas.</p>
				
