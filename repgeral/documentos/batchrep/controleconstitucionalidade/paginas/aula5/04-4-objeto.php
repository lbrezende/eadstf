				<h4>4.4 Objeto</h4>
			</div>
			<div class="col s12 l6">
				
				<p>O artigo 1º da Lei 9.882/1999 afirma que a arguição de descumprimento de preceito fundamental terá por objeto “reparar lesão a preceito fundamental, resultante de <strong>ato do Poder Público</strong>”.</p>
				
				<p>O conceito de ato do Poder Público é bastante genérico e alcança grande variedade de comportamentos estatais. Basicamente qualquer ato de autoridade pública, seja ele normativo, judicial ou administrativo, é passível, em princípio, de ser alvo de ADPF, desde que respeitadas outras condições.</p>
				
				<p>Uma dessas condições é o requisito da subsidiariedade. Segundo o artigo 4º, § 1º, da Lei 9.882/1999, não será admitida arguição de descumprimento de preceito fundamental quando houver outro meio eficaz de sanar a lesividade. Isso, na prática, quer dizer que, se o descumprimento da Constituição que se pretende combater pode ser objeto de ação direta de inconstitucionalidade, de ação declaratória de constitucionalidade ou de ação direta de inconstitucionalidade por omissão, então não cabe a arguição de descumprimento de preceito fundamental. Se, por outro lado, nenhuma dessas se amolda ao problema que se pretende ver solucionado, então pode ser o caso de se propor uma ADPF.</p>
				
			</div>
			<div class="col s12 l6">
				
				<p>Assim, não cabe a ADPF, por exemplo, para questionar a constitucionalidade de uma lei aprovada em 2015, porque as leis aprovadas na vigência da Constituição podem ter sua constitucionalidade impugnada por ação direta de inconstitucionalidade. Por outro lado, é possível propor uma ADPF para discutir a incompatibilidade de uma lei de 1950 que ainda esteja sendo aplicada, com os preceitos fundamentais da Constituição. Isso porque, como as leis anteriores à promulgação da Constituição em 1988 não podem ser alvo de ADI, abre-se a via da ADPF.</p>
				
				<p>A ADPF é, também, meio pelo qual o Supremo Tribunal Federal pode exercer o controle abstrato sobre leis municipais, quando estas forem particularmente relevantes e afetem os preceitos essenciais da Constituição.</p>
				
				
