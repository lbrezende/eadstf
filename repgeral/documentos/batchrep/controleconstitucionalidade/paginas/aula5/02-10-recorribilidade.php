				<h4>2.10 Recorribilidade</h4>
				
				<p>Contra a decisão do Plenário do Supremo Tribunal Federal que julga a ação direta de inconstitucionalidade por omissão também não cabe recurso, salvo a oposição de embargos de declaração para esclarecer eventual ponto que tenha ficado omisso, contraditório ou obscuro no julgamento.</p>
				
				<p>O Procurador-Geral da República interpôs embargos de declaração no julgamento da ADO 22, por entender haver obscuridade quanto aos efeitos da decisão do Supremo Tribunal Federal em relação aos órgãos do Poder Judiciário e à Administração Pública Federal, Estadual e Municipal. Para ele, não estava claro se decisões anteriores do Tribunal Regional Federal da 4ª Região que haviam proibido a publicidade de bebidas com teor alcóolico superior a 0,5°GL seriam atingidas pelos efeitos da decisão do STF na ADO 22.</p>
				
				<p>Os embargos declaratórios foram rejeitados pelo Plenário do STF, que afirmou não haver nenhuma dúvida sobre a eficácia vinculante da sua própria decisão e ressaltou que o Poder Judiciário e a Administração Pública não podem ampliar as restrições estabelecidas pela Lei 9.294/1996 para bebidas alcoólicas com teor inferior a 13°GL, porque apenas o Poder Legislativo tem autorização constitucional para criar novas restrições desse tipo.</p>
				
				
