				<h4>2.3 Objeto</h4>
			</div>
			<div class="col s12 l6 offset-l3">
				
				<p>O objeto da ADO é a omissão no cumprimento de dever constitucional de legislar (Poder Legislativo) ou de adotar providências de caráter administrativo (Poder Executivo). Entretanto, não é qualquer omissão que abre as portas para a ADO. O determinante é que essa omissão prejudique a efetividade de uma norma constitucional.</p>
				
				<p>No caso da ADO 22, o Procurador-Geral da República entendeu que a norma constitucional que exigia a imposição de restrições legais à propaganda comercial de bebidas alcoólicas não estava sendo efetivada em sua totalidade. O Supremo Tribunal, contudo, entendeu que a lei existente sobre a matéria era suficiente para tornar a norma constitucional eficaz. Dito isso, vamos fazer um parêntesis para falar sobre o que é a eficácia das normas constitucionais.</p>
				
