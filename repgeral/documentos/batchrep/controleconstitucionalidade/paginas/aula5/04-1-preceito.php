				<h4>4.1 O que é um preceito fundamental?</h4>
				
				<p>Muito se discute sobre o que significa “descumprimento de preceito fundamental”. É a mesma coisa que descumprimento da Constituição? É sinônimo de inconstitucionalidade? As decisões do STF sinalizam que não. Descumprimento de preceito fundamental não é sinônimo de inconstitucionalidade. Nem todos os preceitos da Constituição são fundamentais. Em linhas bem gerais, preceitos fundamentais são as normas que formam o núcleo da Constituição, a sua essência.</p>
				
				<p>Nesse sentido, as hipóteses de cabimento da arguição de descumprimento de preceito fundamental são mais estritas do que as da ação direta de inconstitucionalidade. Enquanto o descumprimento de qualquer norma da Constituição dá ensejo a esta última, somente a violação dos preceitos fundamentais da Constituição justifica a propositura da ADPF.</p>
