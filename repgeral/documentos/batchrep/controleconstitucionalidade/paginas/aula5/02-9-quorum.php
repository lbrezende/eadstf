				<h4>2.9 Quórum</h4>
				
				<p>Os preceitos da Lei 9.868/1999, referentes à ação direta de inconstitucionalidade, também valem para a ação direta de inconstitucionalidade por omissão. O seu julgamento depende da presença, em sessão, de pelo menos oito Ministros, sendo necessário o mínimo de seis votos para que seja declarada a inconstitucionalidade por omissão.</p>
				
				<p>A ADO 22 foi julgada improcedente na sessão plenária de 22-4-2015, por unanimidade, com o voto de oito Ministros, atingido assim o quórum mínimo de julgamento. Embora nove estivessem presentes à sessão, um deles estava impedido.</p>
				
