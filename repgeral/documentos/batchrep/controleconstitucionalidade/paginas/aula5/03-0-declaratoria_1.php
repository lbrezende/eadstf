				<h3>3. A ação declaratória de constitucionalidade</h3>
			</div>
			<div class="col s12 l8">
				
				<p>O terceiro instrumento de controle concentrado sobre o qual vamos conversar é a <strong>ação declaratória de constitucionalidade (ADC)</strong>, que funciona como uma ação direta de inconstitucionalidade com o sinal invertido. Enquanto a finalidade da ação direta de inconstitucionalidade é obter do Supremo Tribunal Federal uma declaração de que determinada lei ou ato normativo é inconstitucional e, portanto, inválida, o objeto da ação declaratória de constitucionalidade é obter um pronunciamento do Supremo Tribunal Federal no sentido de que determinada lei ou ato normativo federal é constitucional.</p>
				
				<div class="box box-citacao">
					
					<p class="saudacao center">Mas espere um pouco! Por que se buscar a declaração de constitucionalidade de uma norma? </p>
					
					<p class="saudacao center">Não se presume que as leis, uma vez editadas, sejam constitucionalmente válidas? </p>
					
					<p class="saudacao center">Afinal, se existe a ação direta de inconstitucionalidade para que uma lei seja declarada inconstitucional e, com isso, invalidada, não é exatamente porque as leis são, em regra, válidas? </p>
					
					<p class="saudacao center">Então, qual é a justificativa para a existência de uma ação declaratória de constitucionalidade? </p>
					
					<p class="saudacao center">Não se trata de um processo cujo objetivo é afirmar o óbvio? </p>
					
				</div>
				
				<p class="saudacao center">Sim e não.</p>
				
			</div>
			<div class="col s12 l4">
				
				<p>Lembra quando falamos sobre os diferentes tipos de controle de constitucionalidade e destacamos que, no sistema brasileiro, convivem o controle difuso e o concentrado? E lembra também que o controle difuso é exercido por todos os juízes e tribunais do país?</p>
				
				<p>Agora imagine que uma lei é aplicada regularmente por metade dos juízes do país, mas a outra metade não a aplica porque entende que ela seja inconstitucional. Nesse caso, há interesse em que a controvérsia sobre a constitucionalidade da lei seja solucionada de uma vez. Mas, se o seu interesse for a solução dessa controvérsia em sentido favorável à constitucionalidade da lei, não há razão para o ajuizamento de uma ação direta para pedir que ela seja declarada inconstitucional.</p>
				
				<p>Situações como essa, em que a prática dos tribunais leva a um estado de incerteza sobre a constitucionalidade de uma lei, são a justificativa para a existência da ação declaratória de constitucionalidade.</p>
				
