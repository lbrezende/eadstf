				<p>Assim como na ação direta de inconstitucionalidade e na ação direta de inconstitucionalidade por omissão, o processo e o julgamento da ação declaratória de constitucionalidade também é regulado pela Lei 9.868/1999 e, como você já deve esperar, são muito semelhantes em suas linhas gerais.</p>
				
				<p>Quanto à competência (quem julga), ao quórum de julgamento (número de votos), à legitimidade ativa (quem pode propor a ação), à possibilidade de participação de <em>amicus curiae</em> e ao processamento no Tribunal, não há diferenças significativas em relação à ADI.</p>
				
				<p>Assim como ocorre com a ação direta de inconstitucionalidade, a decisão na ação declaratória de constitucionalidade tem eficácia contra todos e efeito vinculante, e contra ela o único recurso cabível também são os embargos de declaração.</p>
				
				<p>O seu objeto, no entanto, é mais restrito do que o da ADI. Ao estudar a ação direta de inconstitucionalidade, vimos que o seu objeto pode ser lei ou ato normativo federal ou estadual. Já a ação declaratória de constitucionalidade somente pode ser ajuizada em relação a lei ou ato normativo federal. Ao contrário da ADI, não se incluem no objeto na ADC as leis e os atos normativos estaduais.</p>
				
				<p class="saudacao center">Por fim, como decide o Tribunal na ação declaratória de constitucionalidade?</p>
				
			</div>
			<div class="col s12 l6">
				
				<p>Se entender que a lei questionada é constitucional, a ação declaratória de constitucionalidade é julgada procedente. Se, no entanto, concluir que ela é inconstitucional, julga improcedente a ação declaratória de constitucionalidade. Como você deve ter percebido, é exatamente o oposto do que acontece no julgamento da ação direta de inconstitucionalidade.</p>
				
