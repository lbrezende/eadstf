				<p>Por fim, há as <strong>normas de eficácia limitada</strong>, que não são autoaplicáveis porque dependem da existência de leis para concretizá-las. São essas, e só essas, que importam para a ação direta de inconstitucionalidade por omissão.</p>
				
				<p>Por exemplo: a Constituição assegura aos trabalhadores um salário mínimo, mas não fixa o seu valor. Apenas diz que será fixado em lei. Nenhum trabalhador pode receber pagamento inferior ao salário mínimo. Mas, sem a existência de uma lei fixando o valor do salário mínimo, não seria possível reclamar do recebimento de um valor muito baixo, como se fosse inferior ao mínimo.</p>
				
				<p>O artigo 220, § 4º, da CF, ao estipular que determinados produtos, em razão da sua nocividade, terão a publicidade restringida por lei, também se encaixa nesse tipo de norma, pois depende necessariamente, para ter eficácia, da edição de uma lei.</p>
				
				<p>Somente as normas constitucionais de eficácia limitada justificam a propositura da ação direta de inconstitucionalidade por omissão. Quanto às demais normas, a sua previsão na Constituição é suficiente para que o seu cumprimento seja exigido.</p>
				
