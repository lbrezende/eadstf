				<h4>2.8 Decisão</h4>
			</div>
			<div class="col s12 l6">
				
				<p>Por fim, decorridos os prazos para as manifestações, o Relator lança o relatório e solicita ao Presidente do Tribunal a inclusão do processo na pauta do Plenário.</p>
				
				<p>A sessão de julgamento da ação direta de inconstitucionalidade por omissão é semelhante à da ação direta de inconstitucionalidade. Após a leitura do relatório, a cargo do Ministro que foi sorteado como Relator, é concedida a palavra para que o autor da ação, as autoridades responsáveis pela edição do ato impugnado, o Advogado-Geral da União, o Procurador-Geral da República e, se for o caso, os amici curiae apresentem suas razões. Em seguida, os Ministros passam a expor seus votos.</p>
				
				<p>No julgamento da ação direta de inconstitucionalidade por omissão, o Supremo Tribunal Federal pode decidir das seguintes formas.</p>
				
				<ol class="lista lista-alpha lista-n lista-n0">
					<li><strong>Declarar a inconstitucionalidade por omissão total:</strong></li>
				</ol>
				
				<p>Se a norma constitucional em relação à qual foi feito o pedido está completamente ineficaz, devido à inércia da autoridade responsável pela adoção da medida para torná-la eficaz, o Tribunal declara a inconstitucionalidade por omissão e cientifica o Poder competente da decisão para a adoção das providências necessárias. A ação direta de inconstitucionalidade por omissão é julgada procedente.</p>
				
			</div>
			<div class="col s12 l6">
				
				<ol class="lista lista-alpha lista-n lista-n1">
					<li><strong>Declarar a inconstitucionalidade por omissão parcial:</strong></li>
				</ol>
				<p>Se a norma constitucional em questão foi objeto de medida legislativa visando a conferir-lhe eficácia, mas essa medida se mostra insuficiente para atender ao comando constitucional, a inércia ou omissão da autoridade responsável pela adoção da medida é apenas parcial. O Tribunal declara a inconstitucionalidade por omissão parcial e cientifica o Poder competente da decisão para a adoção das providências complementares necessárias. A ação direta de inconstitucionalidade por omissão é julgada procedente ou parcialmente procedente, a depender da extensão do pedido.</p>
				
				<ol class="lista lista-alpha lista-n lista-n2">
					<li><strong>Declarar a ausência de omissão inconstitucional:</strong></li>
				</ol>
				
				<p>O Tribunal entende que o Poder Legislativo não está “devendo” a edição da lei necessária para tornar eficaz a norma constitucional e julga improcedente a ação direta de inconstitucionalidade por omissão. É o que fez o Supremo Tribunal Federal na <strong>ADO 22</strong>: julgou-a improcedente, ao entendimento de que a Lei 9.294/1996 não era omissa quanto à regulamentação da propaganda comercial de bebidas alcoólicas exigida pelo artigo 220, § 4º, da Constituição Federal.</p>
				
